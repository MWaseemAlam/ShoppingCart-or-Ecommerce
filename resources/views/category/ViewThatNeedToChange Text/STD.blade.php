<h1>Facts about STDs</h1>
<p>The term sexually transmitted disease (STD) is used to refer to a condition passed from one person to another through sexual contact. You
can contract an STD by having unprotected vaginal, anal, or oral sex with someone who has the STD.</p>
<p>An STD may also be called a sexually transmitted infection (STI) or venereal disease (VD).</p>
<p>That doesn’t mean sex is the only way STDs are transmitted. Depending on the specific STD, infections may also be transmitted through
sharing needles and breastfeeding.</p>
<h1>Symptoms of STDs in men</h1>
<p>It’s possible to contract an STD without developing symptoms. But some STDs cause obvious symptoms.
In men, common symptoms include:</p>
<li>pain or discomfort during sex or urination</li>
<li>sores, bumps, or rashes on or around the penis, testicles, anus, buttocks, thighs, or mouth</li>
<li>unusual discharge or bleeding from the penis</li>
<li>painful or swollen testicles</li>
<h1>Symptoms of STDs in women</h1>
<p>In many cases, STDs don’t cause noticeable symptoms. When they do, common STD symptoms in women include:</p>
<li>pain or discomfort during sex or urination</li>
<li>sores, bumps, or rashes on or around the vagina, anus, buttocks, thighs, or mouth</li>
<li>unusual discharge or bleeding from the vagina</li>
<li>itchiness in or around the vagina</li>
<p>The specific symptoms can vary from one STD to another.</p>
<h1>Types of STDs</h1>
<p>Many different types of infections can be transmitted sexually. The most common STDs are described below.</p>
