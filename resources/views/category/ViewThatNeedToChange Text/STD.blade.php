<p>Many people with chlamydia have no noticeable symptoms. When symptoms do develop, they often include:</p>
<li>pain or discomfort during sex or urination</li>
<li>green or yellow discharge from the penis or vagina</li>
<li>green or yellow discharge from the penis or vagina</li>
<p>If left untreated, chlamydia can lead to:</p>
<li>infections of the urethra, prostate gland, or testicles</li>
<li>pelvic inflammatory disease</li>
<li>infertility</li>
<p>If a pregnant woman has untreated chlamydia, she can pass it to her baby during birth. The baby may develop:</p>
<li>pneumonia</li>
<li>eye infections</li>
<li>blindness</li>
<p>Antibiotics can easily treat chlamydia.</p>
<h1>HPV (human papillomavirus)</h1>
<p>Human papillomavirus (HPV) is a virus that can be passed from one person to another through intimate skin-to-skin or
sexual contact. There are many different strains of the virus. Some are more dangerous than others.</p>
<p>The most common symptom of HPV is warts on the genitals, mouth, or throat.</p>
<p>Some strains of HPV infection can lead to cancer, including:</p>
<li>oral cancer</li>
<li>cervical cancer</li>
<li>vulvar cancer</li>
<li>penile cancer</li>
<li>rectal cancer</li>
<p>While most cases of HPV don’t become cancerous, some strains of the virus are more likely to cause cancer than others. According to the ,
most cases of HPV-related cancer in the United States are caused by HPV 16 and HPV 18. These two strains of HPV account for 70 percent of
all cervical cancer cases.</p>
<p>There’s no treatment for HPV. However, HPV infections often clear up on their own. There’s also a vaccine available to protect against
some of the most dangerous strains, including HPV 16 and HPV 18.
If you contract HPV, proper testing and screenings can help your doctor assess and manage your risk of complications.</p>
<h1>Syphilis</h1>
<p>Syphilis is another bacterial infection. It often goes unnoticed in its early stages.</p>
<p>The first symptom to appear is a small round sore, known as a chancre. It can develop on your genitals, anus, or mouth.
It’s painless but very infectious.</p>
<p>Later symptoms of syphilis can include:</p>
<li>rash</li>
<li>fatigue</li>
<li>fever</li>
<li>headaches</li>
<li>joint pain</li>
<li>weight loss</li>
<li>hair loss</li>
<p>If left untreated, late-stage syphilis can lead to:</p>
<li>loss of vision</li>
<li>loss of hearing</li>
<li>loss of memory</li>
<li>mental illness</li>
<li>infections of the brain or spinal cord</li>
<li>heart disease</li>
<li>death</li>
<p>Fortunately, if caught early enough, syphilis is easily treated with antibiotics. However, syphilis infection in a newborn can be fatal.
That’s why it’s important for all pregnant women to be screened for syphilis.The earlier syphilis is diagnosed and treated, the less damage it does.</p>
