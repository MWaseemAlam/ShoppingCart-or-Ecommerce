<h1>What is HIV?</h1>
<p>HIV is a virus that damages the immune system. The immune system helps the body fight off infections. Untreated HIV infects and kills
CD4 cells, which are a type of immune cell called T cells. Over time, as HIV kills more CD4 cells, the body is more likely to get various
types of infections and cancers.</p>
<p>HIV is transmitted through bodily fluids that include:</p>
<li>blood</li>
<li>semen</li>
<li>vaginal and rectal fluids</li>
<li>breast milk</li>
<p>The virus doesn’t spread in air or water, or through casual contact.</p>
<p>HIV is a lifelong condition and currently there is no cure, although many scientists are working to find one. However, with medical care,
including treatment called antiretroviral therapy, it’s possible to manage HIV and live with the virus for many years.</p>
<p>Without treatment, a person with HIV is likely to develop a serious condition called AIDS. At that point, the immune system is too weak to
fight off other diseases and infections. Untreated, life expectancy with AIDS is about . With antiretroviral therapy, HIV can be
well-controlled and life expectancy can be nearly the same as someone who has not contracted HIV.HIV can cause changes throughout the body.</p>
<h1>What is AIDS?</h1>
<p>AIDS is a disease that can develop in people with HIV. It’s the most advanced stage of HIV. But just because a person has HIV doesn’t
mean they’ll develop AIDS.</p>
<p>HIV kills CD4 cells. Healthy adults generally have a CD4 count of 500 to 1,500 per cubic millimeter. A person with HIV whose CD4 count
falls below 200 per cubic millimeter will be diagnosed with AIDS.</p>
<p>A person can also be diagnosed with AIDS if they have HIV and develop an opportunistic infection or cancer that’s rare in people who don’t
have HIV. An opportunistic infection, such as pneumonia, is one that takes advantage of a unique situation, such as HIV.</p>
<p>Untreated, HIV can progress to AIDS within a decade. There’s no cure for AIDS, and without treatment, life expectancy after diagnosis is
about . This may be shorter if the person develops a severe opportunistic illness. However, treatment with antiretroviral drugs can prevent
AIDS from developing.</p>
<p>If AIDS does develop, it means that the immune system is severely compromised. It’s weakened to the point where it can no longer fight off
most diseases and infections. That makes the person vulnerable to a wide range of illnesses, including:</p>
<li>pneumonia</li>
<li>tuberculosis</li>
<li>oral thrush, a fungal infection in the mouth or throat</li>
<li>cytomegalovirus (CMV), a type of herpes virus</li>
<li>cryptococcal meningitis, a fungal infection in the brain</li>
<li>toxoplasmosis, a brain infection caused by a parasite</li>
<li>cryptosporidiosis, an infection caused by an intestinal parasite</li>
<li>cancer, including Kaposi’s sarcoma (KS) and lymphoma</li>
<h1>HIV and AIDS: What’s the connection?</h1>
<p>To develop AIDS, a person has to have contracted HIV. But having HIV doesn’t necessarily mean that someone will develop AIDS.</p>
<p>Cases of HIV progress through three stages:</p>
<li><b>stage 1:</b> acute stage, the first few weeks after transmission</li>
<li><b>stage 2:</b> clinical latency, or chronic stage</li>
<li><b>stage 3:</b> AIDS</li>
<p>How quickly a case of HIV progresses through the chronic stage varies significantly from person to person. Without treatment, it can last
up to a decade before advancing to AIDS. With treatment, it can last indefinitely.</p>
<p>There is no cure for HIV, but it can be controlled. People with HIV often have a near-normal lifespan with early treatment with
antiretroviral therapy. Along those same lines, there’s technically no cure for AIDS. However, treatment can increase a person’s CD4
count to the point where they’re considered to no longer have AIDS. (This point is a count of 200 or higher.) Also, treatment can
typically help manage opportunistic infections.HIV and AIDS are related, but they’re not the same thing.</p>
<h1>HIV transmission: Know the facts</h1>
<p>Anyone can contract HIV. The virus is transmitted in bodily fluids that include:</p>
<li>blood</li>
<li>semen</li>
<li>vaginal and rectal fluids</li>
<li>breast milk</li>
<p>Some of the ways HIV is spread from person to person include:</p>
<li>through vaginal or anal sex — the most common route of transmission, especially among men who have sex with men.</li>
<li>by sharing needles, syringes, and other items for injection drug use</li>
<li>by sharing tattoo equipment without sterilizing it between uses</li>
<li>during pregnancy, labor, or delivery from a woman to her baby</li>
<li>during breastfeeding</li>
<li>through “pre-mastication,” or chewing a baby’s food before feeding it to them</li>
<li>through exposure to the blood of someone living with HIV, such as through a needle stick</li>
<p>The virus can also be transmitted through a blood transfusion or organ and tissue transplant. However, rigorous testing for HIV among
blood, organ, and tissue donors ensures that this is very rare in the United States.</p>
<p>It’s theoretically possible, but considered extremely rare, for HIV to spread through:</p>
<li>oral sex (only if there are bleeding gums or open sores in the person’s mouth)</li>
<li>being bitten by a person with HIV (only if the saliva is bloody or there are open sores in the person’s mouth)</li>
<li>contact between broken skin, wounds, or mucous membranes and the blood of someone living with HIV</li>
<p>HIV does NOT spread through:</p>
<li>skin-to-skin contact</li>
<li>hugging, shaking hands, or kissing</li>
<li>air or water</li>
<li>sharing food or drinks, including drinking fountains</li>
<li>saliva, tears, or sweat (unless mixed with the blood of a person with HIV)</li>
<li>sharing a toilet, towels, or bedding</li>
<li>mosquitoes or other insects</li>
<p>It’s important to note that if a person with HIV is being treated and has a persistently undetectable viral load, it’s virtually
impossible to transmit the virus to another person.</p>
<h1>Causes of HIV</h1>
<p>HIV is a variation of a virus that infects African chimpanzees. Scientists suspect the simian immunodeficiency virus (SIV) jumped from
chimps to humans when people consumed infected chimpanzee meat. Once inside the human population, the virus mutated into what we now
know as HIV. This likely occurred as long ago as the 1920s.</p>
<p>HIV spread from person to person throughout Africa over the course of several decades. Eventually, the virus migrated to other parts
of the world. Scientists first discovered HIV in a human blood sample in 1959.</p>
<h1>Causes of AIDS</h1>
<p>AIDS is caused by HIV. A person can’t get AIDS if they haven’t contracted HIV.</p>
<p>Healthy individuals have a CD4 count of 500 to 1,500 per cubic millimeter. Without treatment, HIV continues to multiply and destroy
CD4 cells. If a person’s CD4 count falls below 200, they have AIDS.</p>
<p>Also, if someone with HIV develops an opportunistic infection associated with HIV, they can still be diagnosed with AIDS, even if their
CD4 count is above 200.</p>
<h1>What tests are used to diagnose HIV?</h1>
<p>Several different tests can be used to diagnose HIV. Healthcare providers determine which test is best for each person.</p>
<h1>Antibody/antigen tests</h1>
<p>Antibody/antigen tests are the most commonly used tests. They can show positive results typically within after someone initially
contracts HIV.</p>
<p>These tests check the blood for antibodies and antigens. An antibody is a type of protein the body makes to fight an infection. An
antigen, on the other hand, is the part of the virus that activates the immune system.</p>
<h1>Antibody tests</h1>
<p>These tests check the blood solely for antibodies. Between after transmission, most people will develop detectable HIV antibodies,
which can be found in the blood or saliva.</p>
<p>These tests are done using blood tests or mouth swabs, and there’s no preparation necessary. Some tests provide results in 30 minutes
or less and can be performed in a healthcare provider’s office or clinic.</p>
<p>Other antibody tests can be done at home:</p>
<li><b>OraQuick HIV Test.</b> An oral swab provides results in as little as 20 minutes.</li>
<li><b>Home Access HIV-1 Test System.</b> After the person pricks their finger, they send a blood sample to a licensed laboratory. They can remain
anonymous and call for results the next business day.</li>
<h1>Nucleic acid test (NAT)</h1>
<p>This expensive test isn’t used for general screening. It’s for people who have early symptoms of HIV or have a known risk factor. This
test doesn’t look for antibodies; it looks for the virus itself. It takes from 5 to 21 days for HIV to be detectable in the blood. This
test is usually accompanied or confirmed by an antibody test.</p>
<h1>What’s the HIV window period?</h1>
<p>As soon as someone contracts HIV, it starts to reproduce in their body. The person’s immune system reacts to the antigens (parts of the
virus) by producing antibodies (cells that fight the virus).</p>
<p>The time between exposure to HIV and when it becomes detectable in the blood is called the HIV window period. Most people develop
detectable HIV antibodies within 23 to 90 days after infection.</p>
<p>If a person takes an HIV test during the window period, it’s likely they’ll receive a negative result. However, they can still transmit
the virus to others during this time. If someone thinks they may have been exposed to HIV but tested negative during this time, they
should repeat the test in a few months to confirm (the timing depends on the test used).</p>
<p>Someone who tests negative during the window might benefit from post-exposure prophylaxis (PEP). This is medication taken after an
exposure to prevent getting HIV. PEP needs to be taken as soon as possible after the exposure; it should be taken no later than 72
hours after exposure, but ideally before then.</p>
<p>Another way to prevent getting HIV is pre-exposure prophylaxis (PrEP). A combination of HIV drugs taken before potential exposure to
HIV, PrEP can lower the risk of contracting or spreading HIV when taken consistently.</p>
<p>Timing is important when testing for HIV.</p>
<h1>Early symptoms of HIV</h1>
<p>The first few weeks after someone contracts HIV is called the acute infection stage. During this time, the virus reproduces rapidly.
The person’s immune system responds by producing HIV antibodies. These are proteins that fight infection.</p>
<p>During this stage, some people have no symptoms at first. However, many people experience symptoms in the first month or two after
contracting the virus, but often don’t realize they’re caused by HIV. This is because symptoms of the acute stage can be very similar to
those of the flu or other seasonal viruses. They may be mild to severe, they may come and go, and they may last anywhere from a few days
to several weeks.</p>
<p>Early symptoms of HIV can include:</p>
<li>fever</li>
<li>chills</li>
<li>swollen lymph nodes</li>
<li>general aches and pains</li>
<li>skin rash</li>
<li>sore throat</li>
<li>headache</li>
<li>nausea</li>
<li>upset stomach</li>
<p>Because these symptoms are similar to common illnesses like the flu, the person with them might not think they need to see a healthcare
provider. And even if they do, their healthcare provider might suspect the flu or mononucleosis and might not even consider HIV.</p>
<p>Whether a person has symptoms or not, during this period their viral load is very high. The viral load is the amount of HIV found in
the bloodstream. A high viral load means that HIV can be easily transmitted to someone else during this time.</p>
<p>Initial HIV symptoms usually resolve within a few months as the person enters the chronic, or clinical latency, stage of HIV. This
stage can last many years or even decades with treatment.HIV symptoms can vary from person to person.</p>
<h1>What are the symptoms of HIV?</h1>
<p>After the first month or so, HIV enters the clinical latency stage. This stage can last from a few years to a few decades. Some people
don’t have any symptoms during this time, while others may have minimal or nonspecific symptoms. A nonspecific symptom is a symptom that
doesn’t pertain to one specific disease or condition.</p>
<p>These nonspecific symptoms may include:</p>
<li>headaches and other aches and pains</li>
<li>swollen lymph nodes</li>
<li>recurrent fevers</li>
<li>night sweats</li>
<li>fatigue</li>
<li>nausea</li>
<li>vomiting</li>
<li>diarrhea</li>
<li>weight loss</li>
<li>skin rashes</li>
<li>recurrent oral or vaginal yeast infections</li>
<li>pneumonia</li>
<li>shingles</li>
<p>As with the early stage, HIV is still infectious during this time even without symptoms and can be transmitted to another person.
However, a person won’t know they have HIV unless they get tested. If someone has these symptoms and thinks they may have been exposed
to HIV, it’s important that they get tested.</p>
<p>HIV symptoms at this stage may come and go, or they may progress rapidly. This progression can be slowed substantially with treatment.
With the consistent use of this antiretroviral therapy, chronic HIV can last for decades and will likely not develop into AIDS, if
treatment was started early enough.</p>
<h1>Is rash a symptom of HIV?</h1>
<p>About 90 percent of people with HIV experience changes to their skin. Rash is often one of the first symptoms of HIV infection. Generally,
an HIV rash appears as multiple small red lesions that are flat and raised.</p>
<h1>Rash related to HIV</h1>
<p>HIV makes someone more susceptible to skin problems because the virus destroys immune system cells that fight infection. Co-infections
that can cause rash include:</p>
<li>molluscum contagiosum</li>
<li>herpes simplex</li>
<li>shingles</li>
<p>The appearance of the rash, how long it lasts, and how it can be treated depends on the cause.</p>
<h1>Rash related to medication</h1>
<p>While rash can be caused by HIV co-infections, it can also be caused by medication. Some drugs used to treat HIV or other infections can
cause a rash. This type of rash usually appears within a week or two of starting a new medication. Sometimes the rash will clear up on its
own. If it doesn’t, a change in medications may be needed.</p>
<p>Rash due to an allergic reaction to medication can be serious. Other symptoms of an allergic reaction include trouble breathing or
swallowing, dizziness, and fever.</p>
<p>Stevens-Johnson syndrome (SJS) is a rare allergic reaction to HIV medication. Symptoms include fever and swelling of the face and tongue.
A blistering rash, which can involve the skin and mucous membranes, appears and spreads quickly. When 30 percent of the skin is affected
it’s called toxic epidermal necrolysis, which is a life-threatening condition. If this develops, emergency medical care is needed.</p>
<p>While rash can be linked with HIV or HIV medications, it’s important to keep in mind that rashes are common and can have many other causes.</p>
<h1>HIV symptoms in men: Is there a difference?</h1>
<p>Symptoms of HIV vary from person to person, but they’re similar in men and women. These symptoms can come and go or get progressively worse.</p>
<p>If a person has been exposed to HIV, they may also have been exposed to other sexually transmitted infections (STIs). These include
gonorrhea, chlamydia, syphilis, and trichomoniasis. Men may be more likely than women to notice symptoms of STIs such as sores on their
genitals. However, men typically don’t seek medical care as often as women.</p>
<h1>HIV symptoms in women: Is there a difference?</h1>
<p>For the most part, symptoms of HIV are similar in men and women. However, symptoms they experience overall may differ based on the
different risks men and women face if they have HIV.</p>
<p>Both men and women with HIV are at increased risk of sexually transmitted infections (STIs). However, women may be less likely than
men to notice small spots or other changes to their genitals.</p>
<p>In addition, women with HIV are at increased risk of:</p>
<li>recurrent vaginal yeast infections</li>
<li>other vaginal infections, including bacterial vaginosis</li>
<li>pelvic inflammatory disease (PID)</li>
<li>menstrual cycle changes</li>
<li>human papillomavirus (HPV), which can cause genital warts and lead to cervical cancer</li>
<p>While not related to HIV symptoms, another risk for women with HIV is that the virus can be transmitted to a baby during pregnancy.
However, antiretroviral therapy is considered safe during pregnancy. Women who are treated with antiretroviral therapy are at very
low risk of passing HIV to their baby during pregnancy and delivery.</p>
<p>Breastfeeding is also affected in women with HIV. The virus can be passed to a baby through breast milk. In the United States and
other settings where formula is accessible and safe, it’s recommended that women with HIV not breastfeed their babies. For these women,
use of formula is encouraged. Options besides formula include pasteurized banked human milk (https://www.hmbana.org).</p>
<h1>What are the symptoms of AIDS?</h1>
<p>AIDS refers to acquired immunodeficiency syndrome. With this condition, the immune system is weakened due to HIV that’s typically
gone untreated for many years. If HIV is found and treated early with antiretroviral therapy, a person will usually not develop AIDS.</p>
<p>People with HIV may develop AIDS if their HIV is not diagnosed until late, or if they know they have HIV but don’t consistently take
their antiretroviral therapy. They may also develop AIDS if they have a type of HIV that’s resistant to (doesn’t respond to) the
antiretroviral treatment.</p>
<p>Without proper and consistent treatment, people living with HIV can develop AIDS sooner. By that time, the immune system is quite
damaged and has a harder time fighting off infection and disease. With the use of antiretroviral therapy, a person can maintain
chronic HIV infection without developing AIDS for decades.</p>
<p>Symptoms of AIDS can include:</p>
<li>recurrent fever</li>
<li>chronic swollen lymph glands, especially of the armpits, neck, and groin</li>
<li>chronic fatigue</li>
<li>night sweats</li>
<li>dark splotches under the skin or inside the mouth, nose, or eyelids</li>
<li>sores, spots, or lesions of the mouth and tongue, genitals, or anus</li>
<li>bumps, lesions, or rashes of the skin</li>
<li>recurrent or chronic diarrhea</li>
<li>rapid weight loss</li>
<li>neurologic problems such as trouble concentrating, memory loss, and confusion</li>
<li>anxiety and depression</li>
<p>Antiretroviral therapy controls the virus and usually prevents progression to AIDS. Other infections and complications of AIDS can
also be treated. That treatment must be tailored to the individual needs of the person.</p>
<h1>Treatment options for HIV</h1>
<p>Treatment should begin as soon as possible after a diagnosis of HIV, regardless of viral load. The main treatment for HIV is antiretroviral
therapy, a combination of daily medications that stop the virus from reproducing. This helps protect CD4 cells, keeping
the immune system strong enough to fight off disease.</p>
<p>Antiretroviral therapy helps keep HIV from progressing to AIDS. It also helps reduce the risk of transmitting HIV to others.</p>
<p>When treatment is effective, the viral load will be “undetectable.” The person still has HIV, but the virus is not visible in test results.
However, the virus is still in the body. And if that person stops taking antiretroviral therapy, the viral load will increase again and the
HIV can again start attacking CD4 cells.</p>
<h1>HIV medications</h1>
<p>More than 25 antiretroviral therapy medications are approved to treat HIV. They work to prevent HIV from reproducing and destroying CD4
cells, which help the immune system fight infection. This helps reduce the risk of developing complications related to HIV, as well as
transmitting the virus to others.</p>
<p>These antiretroviral medications are grouped into six classes:</p>
<li>nucleoside reverse transcriptase inhibitors (NRTIs)</li>
<li>non-nucleoside reverse transcriptase inhibitors (NNRTIs)</li>
<li>protease inhibitors</li>
<li>fusion inhibitors</li>
<li>CCR5 antagonists, also known as entry inhibitors</li>
<li>integrase strand transfer inhibitors</li>
<h1>Treatment regimens</h1>
<p>The U.S. Department of Health and Human Services (HHS) generally recommends a starting regimen of three HIV medications from at least
two of these drug classes. This combination helps prevent HIV from forming resistance to medications. (Resistance means the drug no
longer works to treat the virus.)</p>
<p>Many of the antiretroviral medications are combined with others so that a person with HIV typically takes only one or two pills a day.</p>
<p>A healthcare provider will help a person with HIV choose a regimen based on their overall health and personal circumstances. These medications
must be taken every day, exactly as prescribed. If they’re not taken appropriately, viral resistance can develop, and a new regimen may
be needed.</p>
<p>Blood testing will help determine if the regimen is working to keep the viral load down and the CD4 count up. If an antiretroviral
therapy regimen isn’t working, the person’s healthcare provider will switch them to a different regimen that’s more effective.</p>
<h1>Side effects and costs</h1>
<p>Side effects of antiretroviral therapy vary and may include nausea, headache, and dizziness. These symptoms are often temporary and
disappear with time. Serious side effects can include swelling of the mouth and tongue and liver or kidney damage. If side effects
are severe, the medications can be adjusted.</p>
<p>Costs for antiretroviral therapy vary according to geographic location and type of insurance coverage. Some pharmaceutical companies
have assistance programs to help lower the cost. </p>
<h1>HIV prevention</h1>
<p>Although many researchers are working to develop one, there’s currently no vaccine available to prevent the transmission of HIV.
However, taking certain steps can help prevent the spread of HIV.</p>
<h1>Safer sex</h1>
<p>The most common way for HIV to spread is through anal or vaginal sex without a condom. This risk can’t be completely eliminated unless
sex is avoided entirely, but the risk can be lowered considerably by taking a few precautions. A person concerned about their risk of
HIV should:</p>
<li><b>Get tested for HIV.</b> It’s important they learn their status and that of their partner.</li>
<li><b>Get tested for other sexually transmitted infections (STIs).</b> If they test positive for one, they should get it treated, because having
an STI increases the risk of contracting HIV.</li>
<li><b>Use condoms.</b> They should learn the correct way to use condoms and use them every time they have sex, whether it’s through vaginal or anal
intercourse. It’s important to keep in mind that pre-seminal fluids (which come out before male ejaculation) can contain HIV.</li>
<li><b>Limit their sexual partners.</b> They should have one sexual partner with whom they have an exclusive sexual relationship.</li>
<li><b>Take their medications as directed if they have HIV.</b> This lowers the risk of transmitting the virus to their sexual partner.</li>
<h1>Other prevention methods</h1>
<p>Other steps to help prevent the spread of HIV include:</p>
<li><b>Avoid sharing needles or other drug paraphernalia.</b> HIV is transmitted through blood and
can be contracted by using contaminated materials.</li>
<li><b>Consider PEP.</b> A person who has been exposed to HIV should contact their healthcare provider about obtaining post-exposure prophylaxis (PEP).
PEP can reduce the risk of contracting HIV. It consists of three antiretroviral medications given for 28 days. PEP should be started as
soon as possible after exposure, but before 36 to 72 hours have passed.</li>
<li><b>Consider PrEP.</b> A person at a high risk of HIV should talk to their healthcare provider about pre-exposure prophylaxis (PrEP). If taken
consistently, it can lower the risk of contracting HIV. PrEP is a combination of two drugs available in pill form.</li>
<h1>Living with HIV: What to expect and tips for coping</h1>
<p>More than 1 million people in the United States are living with HIV. It’s different for everybody, but with treatment, many can expect to
live a long, productive life.</p>
<p>The most important thing is to start antiretroviral treatment as soon as possible. By taking medications exactly as prescribed,people
living with HIV can keep their viral load low and their immune system strong. It’s also important to follow up with a healthcare provider
regularly.</p>
<p>Other ways people living with HIV can improve their health include:</p>
<li><b>Make their health their top priority.</b> Steps to help people living with HIV feel their best include:
<ul>
<li>fueling their body with a well-balanced diet</li>
<li>exercising regularly</li>
<li>getting plenty of rest</li>
<li>avoiding tobacco and other drugs</li>
<li>reporting any new symptoms to their healthcare provider right away</li>
</ul>
</li>
<li><b>Focus on their emotional health.</b> They could consider seeing a licensed therapist who is
experienced in treating people with HIV.</li>
<li><b>Use safer sex practices.</b> Talk to their sexual partner(s). Get tested for other sexually transmitted infections (STIs).
And use condoms every time they have vaginal or anal sex.</li>
<li><b>Talk to their healthcare provider about PrEP and PEP.</b> When used consistently by a person without HIV,pre-exposure prophylaxis (PrEP)
and post-exposure prophylaxis (PEP) can lower the chances of transmission. PrEP is most often recommended for people without HIV in
relationships with people with HIV, but it can be used in other situations as well. Online sources for finding a PrEP provider include
PrEP Locator and PleasePrEPMe.</li>
<li><b>Get support.</b> They can join an HIV support group, either in person or online, so they can meet with others who face the same
concerns they have. And their healthcare provider can steer them toward a variety of resources in their area.</li>
<p>There are many ways to get the most out of life when living with HIV.</p>
<h1>HIV life expectancy: Know the facts</h1>
<p>In the 1990s, a 20-year-old person with HIV had a 19-year life expectancy. By 2011, a 20-year-old person with HIV could expect to
live another 53 years.It’s a dramatic improvement, due in large part to antiretroviral therapy. With proper treatment, many people
with HIV can expect a normal or near normal lifespan.</p>
<p>Of course, many things affect life expectancy for a person with HIV. Among them are:</p>
<li>CD4 cell count</li>
<li>viral load</li>
<li>serious HIV-related illnesses, including hepatitis infection</li>
<li>drug use</li>
<li>smoking</li>
<li>access, adherence, and response to treatment</li>
<li>other health conditions</li>
<li>age</li>
<h1>Is there a vaccine for HIV?</h1>
<p>Currently, there are no vaccines to prevent or treat HIV. Research and testing on experimental vaccines are ongoing, but none are
close to being approved for general use.</p>
<p>HIV is a complicated virus. It mutates (changes) rapidly and is often able to fend off immune system responses. Only a small
number of people who have HIV develop broadly neutralizing antibodies, the kind of antibodies that can fight a range of HIV strains.</p>
<p>The first HIV vaccine efficacy study in seven years is currently underway in South Africa. The experimental vaccine is an updated
version of one used in a 2009 trial that took place in Thailand. A 3.5-year follow-up after vaccination showed the vaccine was 31.2 percent
effective in preventing HIV infection. It’s the most successful HIV vaccine trial to date.</p>
<p>While there’s still no vaccine to prevent HIV, people with HIV can benefit from other vaccines to prevent HIV-related illnesses,
such as:</p>
<li>pneumonia</li>
