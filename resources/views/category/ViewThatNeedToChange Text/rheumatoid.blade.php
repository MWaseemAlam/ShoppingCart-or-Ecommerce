<h1>Talk to your doctor</h1>
<p>RA is a chronic disease that doesn’t currently have a cure. That said, most people with RA don’t have constant symptoms. Instead, they
have flare-ups followed by relatively symptom-free periods, called remissions. The course of the disease varies from person to person
and symptoms can range from mild to severe.</p>
<p>Though symptoms may stop for extended periods, joint problems caused by RA will usually get worse over time. That’s why early treatment
is so important to help delay serious joint damage. If you’re experiencing any symptoms or have concerns about RA, talk to your doctor.</p>
