<h1>What’s rheumatoid arthritis?</h1>
<p>Rheumatoid arthritis (RA) is an autoimmune disease that can cause joint pain and damage throughout your body. The joint damage that RA
causes usually happens on both sides of your body. So if a joint is affected in one of your arms or legs, the same joint in the other
arm or leg will probably be affected, too. This is one way that doctors distinguish RA from other forms of arthritis, such as
osteoarthritis (OA).</p>
<p>Treatments work best when RA is diagnosed early, so it’s important to learn the signs. Read on to learn everything you want
to know about RA, from types and symptoms, to home remedies, diets, and other treatments.</p>
<h1>Rheumatoid arthritis symptoms</h1>
<p>RA is a long-term or chronic disease marked by symptoms of inflammation and pain in the joints. These symptoms and signs
occur during periods known as flares. Other times are known as periods of remission — this is when symptoms dissipate completely.</p>
<p>RA symptoms, which can occur throughout the body, include:</p>
<li>joint pain</li>
<li>joint swelling</li>
<li>joint stiffness</li>
<li>loss of joint function</li>
<p>Symptoms can vary from mild to severe. It’s important not to ignore your symptoms, even if they come and go. Knowing the early
signs of RA will help you and your doctor to better treat it.</p>
<h1>Rheumatoid arthritis diagnosis</h1>
<p>Diagnosing RA can take time and may require multiple lab tests to confirm clinical examination findings. Your doctor will use
several tools to diagnose RA.</p>
<p>First your doctor will ask about your symptoms and medical history. They’ll also perform a physical exam of your joints. This will
include looking for swelling and redness, and testing your reflexes and muscle strength. Your doctor will also touch the affected
joints to check for warmth and tenderness. If they suspect RA, they’ll most likely refer you to a specialist called a rheumatologist.</p>
<p>Since no single test can confirm a diagnosis of RA, your doctor or rheumatologist may use several different types of tests. They may
test your blood for certain substances like antibodies, or check the level of certain substances like acute phase reactants that are
elevated during inflammatory conditions. These can be a sign of RA and help support the diagnosis.</p>
<p>They may also request certain imaging tests. Tests such as ultrasonography, x-ray exams, and magnetic resonance imaging (MRI) not only
show if damage from RA has been done to your joints but also how severe the damage is. A complete evaluation and monitoring of other
organ systems might be in order for some people with RA, too. Learn more about the process of diagnosing RA.</p>
<h1>Blood test for rheumatoid arthritis</h1>
<p>There are several types of blood tests that help your doctor or rheumatologist determine whether you have RA. These tests include:</p>
<li>Rheumatoid factor test: This blood test checks for a protein called rheumatoid factor. High levels of rheumatoid factor are associated
with autoimmune diseases, especially RA.</li>
<li>Anticitrullinated protein antibody test (anti-CCP):This test looks for an antibody that’s associated with RA. People who have this
antibody usually have the disease. However, not everyone with RA tests positive for this antibody.</li>
<li>Antinuclear antibody test: This tests your immune system to see if it’s producing antibodies. Your body may make antibodies as a
response to many different types of conditions, including RA.</li>
<li>Erythrocyte sedimentation rate: This test helps determine the degree of inflammation in your body. The result tells your doctor whether
inflammation is present. However, it doesn’t indicate the cause of the inflammation.</li>
<li>C-reactive protein test: A severe infection or significant inflammation anywhere in your body can trigger your liver to make C-reactive
protein. High levels of this inflammatory marker are associated with RA.</li>
<h1>Rheumatoid arthritis treatment</h1>
<p>There’s no cure for RA, but there are treatments that can help you to manage it. Treatments for RA help to manage the pain and control
the inflammatory response which can in many cases result in remission. Decreasing the inflammation can also help to prevent further
joint and organ damage.</p>
<p>Treatments may include:</p>
<li>medications</li>
<li>alternative or home remedies</li>
<li>dietary changes</li>
<li>specific types of exercise</li>
<p>Your doctor will work with you to determine the best types of treatments for you. For many people, these treatments can help them
live an active life and reduce the risk of long-term complications. Learn more about specific RA treatments and how to treat flares.</p>
<h1>Rheumatoid arthritis medications</h1>
<p>There are many types of medication for RA. Some of these medications help to reduce the pain and inflammation of RA. Some help to reduce
flares and limit the damage that RA does to your joints.</p>
<h1>The following medications help reduce the pain and inflammation during RA flares:</h1>
<li>nonsteroidal anti-inflammatory drugs (NSAIDs)</li>
<li>corticosteroids</li>
<li>acetaminophen</li>
<p>The following drugs work to slow the damage that RA can cause to your body:</p>
<li>Disease-modifying antirheumatic drugs (DMARDs): DMARDs work by blocking your body’s immune system response. This helps to slow
down RA’s progression.</li>
<li>Biologics: These new generation DMARDs provide a targetedresponse to inflammation rather than blocking your body’s entire immune system
response. They may be an effective treatment for people who don’t respond to treatment with more traditional DMARDs.</li>
<li>Janus kinase (JAK) inhibitors: These are a new subcategory of DMARDs that block certain immune responses. These are drugs that your
doctor may use to help prevent inflammation and stop damage to your joints when DMARDs and biologics don’t work for you.</li>
<h1>Home remedies for rheumatoid arthritis</h1>
<p>Certain home remedies and lifestyle adjustments may help to improve your quality of life when living with RA:</p>
<h1>Exercise</h1>
<p>Low-impact exercises can help to improve the range of motion in your joints and increase your mobility. Exercise can also strengthen
muscles, which can help to relieve some of the pressure from your joints. You can also try gentle yoga, which will help you regain
strength and flexibility.</p>
<h1>Get enough rest</h1>
<p>You may need more rest during flare-ups and less during remission. Getting enough sleep will help to reduce inflammation and pain
as well as fatigue.</p>
<h1>Apply heat or cold</h1>
<p>Ice packs can help to reduce inflammation and pain. They may also be effective against muscle spasms. You can alternate cold with hot
treatments such as warm showers and hot compresses. These treatments may help to reduce stiffness.</p>
<h1>Try assistive devices</h1>
<p>Certain devices such as splints and braces can hold your joints in a resting position. This may help to reduce inflammation. Canes and
crutches can help you maintain mobility, even during flares. You can also install household devices, such as grab bars and handrails in
bathrooms and along staircases.</p>
<h1>Rheumatoid arthritis diet</h1>
<p>Your doctor or dietitian may recommend an anti-inflammatory diet for you to help with your symptoms. This type of diet includes foods
that have lots of omega-3 fatty acids.</p>
<p>Foods high in omega-3 fatty acids include:</p>
<li>fatty fishes, including salmon, tuna, herring, and mackerel</li>
<li>chia seeds</li>
<li>flax seeds</li>
<li>walnuts</li>
<p>Antioxidants, such as vitamins A, C, and E, and selenium, may also help reduce inflammation. Foods high in antioxidants include:</p>
<li>berries, such as blueberries, cranberries, goji berries, and strawberries</li>
<li>dark chocolate</li>
<li>spinach</li>
<li>kidney beans</li>
<li>pecans</li>
<li>artichokes</li>
<p>Eating lots of fiber is also important, because according to some researchers, fiber may help reduce inflammatory responses which
can be seen as a decrease in C-reactive protein levels. Choose whole grain foods, fresh vegetables, and fresh fruit. Strawberries may be
particularly beneficial.</p>
<p>Foods containing flavonoids can also help to counter inflammation in the body.They include:</p>
<li>soy products, such as tofu and miso</li>
<li>berries</li>
<li>green tea</li>
<li>broccoli</li>
<li>grapes</li>
<p>What you don’t eat is just as important as what you do eat. Make sure to avoid trigger foods. These include processed carbohydrates and
saturated or trans fats. Avoiding trigger foods and choosing the right foods to follow an anti-inflammatory diet may help you manage
your RA.</p>
<h1>Types of rheumatoid arthritis</h1>
<p>There are several different types of RA. Knowing which type you have may help your doctor provide the best type of treatment for you.
Types of RA include:</p>
<li>Seropositive RA: If you have seropositive RA, you have a positive rheumatoid factor blood test result. This means you have the
antibodies that cause your immune system to attack your joints.</li>
<li>Seronegative RA: If you have a negative rheumatoid factor blood test result and a negative anti-CCP blood test result, but you still
have RA symptoms, you may have seronegative RA. You may eventually develop antibodies, changing your diagnosis to seropositive RA.</li>
<li>JIA (juvenile idiopathic arthritis): Juvenile idiopathic arthritis refers to RA in people younger than 17 years of age. The condition
was previously known as JRA (juvenile rheumatoid arthritis). The symptoms are the same as those of other types of RA, but they may
also include eye inflammation and issues with physical development.</li>
<h1>Seropositive rheumatoid arthritis</h1>
<p>Seropositive RA is the most common type of RA. This type of arthritis may run in families. Seropositive RA may come with more severe
symptoms than seronegative RA. Presenting symptoms of seropositive RA can include:</p>
<li>morning stiffness which lasts 30 minutes or longer</li>
<li>swelling and pain in multiple joints</li>
<li>swelling and pain in symmetrical joints</li>
<li>rheumatoid nodules</li>
<li>fever</li>
<li>fatigue</li>
<li>weight loss</li>
<p>RA isn’t always confined to the joints and some people with seropositive RA can experience inflammation in the eyes, salivary glands,
nerves, kidneys, lungs, heart, skin, and blood vessels.</p>
<h1>Rheumatoid arthritis causes</h1>
<p>The cause of RA isn’t known. However, certain factors seem to have a role in increasing the risk of developing RA or triggering its onset.
Factors that increase risk of RA include:</p>
<li>being a woman</li>
<li>having a family history of RA</li>
<p>Factors that may trigger onset of RA include:</p>
<li>exposure to certain types of bacteria, such as those associated with periodontal disease</li>
<li>having a history of viral infections like infection with the Epstein-Barr virus, which causes mononucleosis</li>
<li>trauma or injury, such as bone breakage or fracture, dislocation of a joint, and ligament damage</li>
<li>smoking cigarettes</li>
<li>obesity</li>
<h1>Rheumatoid arthritis hands</h1>
<p>Arthritis in the hands may start as a low-level burning sensation that you feel at the end of the day. Eventually, you may experience pain
that isn’t necessarily from using your hands. This pain can get quite severe if you don’t treat it. You may also feel swelling, redness,
warmth, and stiffness. If the cartilage in your joints wears away, you may notice some deformities in your hands. You may also have a
grinding feeling in the joints of your hands, fingers, and large joints, if the cartilage deteriorates completely.</p>
<p>As the disease progresses, fluid-filled sacs or synovial cysts may develop in the wrists and around the joints of the hands. These cysts
aren’t without complications and tendon rupture can occur in some cases. You may also develop knobby growths, called bone spurs, in the
affected joints. Over time, bone spurs can make it harder for you to use your hands.</p>
<p>If you have RA in your hands, your doctor will work with you on exercises which can help you to retain movement and function. These,
along with other types of treatment, can help reduce inflammation and stave off progression of the disease. See exactly what the effects
of RA looks like on your hands.</p>
<h1>Rheumatoid arthritis pictures</h1>
<p>RA may be most visible in your hands and feet, particularly as the disease progresses and especially if you don’t have treatment. Swelling
of fingers, wrists, knees, ankles, and toes are common. Damage to ligaments and swelling in the feet can cause a person with RA to have
trouble walking.</p>
<p>If you don’t get treatment for RA, you may develop severe deformities in your hands and feet. Deformities of the hands and fingers may
cause a curved, claw-like appearance. Your toes can also take on a claw-like look, sometimes bending upward, and sometimes curling under
the ball of the foot. You may also notice ulcers, bunions, and calluses on your feet.</p>
<p>Lumps, called rheumatoid nodules, can appear anywhere on your body where joints are inflamed. These can range in size from very small
to the size of a walnut or larger, and they can occur in clusters. This is what rheumatoid nodules and other visible signs of RA look like.</p>
<h1>The difference between rheumatoid arthritis and osteoarthritis</h1>
<p>Like RA, people with OA can experience painful and stiff joints that can make moving around difficult. People with OA may have joint
swelling after extended activity, but OA doesn’t cause any significant inflammatory reaction that typically results in redness of the
affected joints.</p>
<p>Unlike RA, OA isn’t an autoimmune disease. It’s related to the natural wear and tear of the joints as you age or it can develop as a
result of trauma. OA is most often seen in older adults. However, it can sometimes be seen in younger adults who overuse a particular
joint — such as tennis players and other athletes — or those who have experienced a severe injury.</p>
<p>RA is an autoimmune disease. The joint damage from RA isn’t caused by normal wear and tear but by your body attacking itself.
Learn more about these two types of arthritis.</p>
<h1>Is rheumatoid arthritis hereditary?</h1>
<p>Rheumatoid arthritis isn’t considered a hereditary disease, yet it does appear to run in families. This may be due to environmental
causes, genetic causes, or a combination of both. If you have family members who have or have had RA, talk to your doctor, especially
if you have any symptoms of persistent joint pain, swelling, and stiffness, unrelated to overuse or trauma.</p>
