<h1>Nutrition:What is it and why is it important?</h1>
<p>Nutrition, nourishment, or aliment, is the supply of materials - food - required by organisms and cells to stay alive. In science and
human medicine, nutrition is the science or practice of consuming and utilizing foods.</p>
<p>In hospitals, nutrition may refer to the food requirements of patients, including nutritional solutions delivered via an IV
(intravenous) or IG (intragastric) tube.</p>
<p>Nutritional science studies how the body breaks food down (catabolism) and how it repairs and creates cells and tissue (anabolism).
Catabolism and anabolism combined can also be referred to as metabolism. Nutritional science also examines how the body responds to food.</p>
<h1>Fast facts on nutrition</h1>
<li>The human body requires seven major types of nutrients.</li>
<li>Not all nutrients provide energy but are still important, such as water and fiber.</li>
<li>Micronutrients are important but required in smaller amounts.</li>
<li>Vitamins are essential organic compounds that the human body cannot synthesize.</li>
<h1>What is nutrition?</h1>
<p>As molecular biology, biochemistry, and genetics advance, nutrition has become more focused on metabolism and metabolic pathways
- biochemical steps through which substances inside us are transformed from one form to another.</p>
<p>Nutrition also focuses on how diseases, conditions, and problems can be prevented or reduced with a healthy diet.</p>
<p>Similarly, nutrition involves identifying how certain diseases and conditions may be caused by dietary factors, such as
poor diet (malnutrition), food allergies, and food intolerances.</p>
<h1>Dietitian vs. nutritionist</h1>
<p>A registered dietitian nutritionist (RD or RDN) studies food, nutrition, and dietetics through an accredited university and
approved curriculum, then completes a rigorous internship and passes a licensure exam to become a registered dietitian.</p>
<p>A nutritionist (without the title of an RD or RDN) studies nutrition via self-study or through formal education but does not
meet the requirements to use the titles RD or RDN. The two terms are often interchangeable, but they are not identical.</p>
<h1>Dietetics</h1>
<p>Dietetics is the interpretation and communication of the science of nutrition; it helps people make informed and practical
choices about food and lifestyle in both health and disease.</p>
<p>Part of a dietician's course includes both hospital and community settings. Dietitians work in a variety of areas, from
private practice to healthcare, education, corporate wellness, and research, while a much smaller proportion work in the food industry.</p>
<p>A dietitian must have a recognized degree or postgraduate degree in nutrition and dietetics and meet continuing education
requirements to work as a dietitian.</p>
<h1>Nutrition</h1>
<p>Nutrition is the study of nutrients in food, how the body uses nutrients, and the relationship between diet, health, and disease.</p>
<p>Major food manufacturers employ nutritionists and food scientists.</p>
<p>Nutritionists may also work in journalism, education, and research. Many nutritionists work in the field of food science and technology.</p>
<p>There is a lot of overlap between what nutritionists and dietitians do and study. Some nutritionists work in a healthcare setting, some
dietitians work in the food industry, but a higher percentage of nutritionists work in the food industry and in food science and technology,
and a higher percentage of dietitians work in healthcare, corporate wellness, research, and education.</p>
<h1>Types</h1>
<p>A nutrient is a source of nourishment, a component of food, for instance, protein, carbohydrate, fat, vitamin, mineral, fiber, and water.</p>
<li>Macronutrients are nutrients we need in relatively large quantities.</li>
<li>Micronutrients are nutrients we need in relatively small quantities.</li>
<p>Macronutrients can be further split into energy macronutrients (that provide energy), and macronutrients that do not provide energy.</p>
<h1>Energy macronutrients</h1>
<p>Energy macronutrients provide energy, which is measured either in kilocalories (kcal or calories) or Joules. 1 kilocalorie
  (calorie) = 4185.8 joules. Energy macronutrients include:</p>
<h1>Carbohydrates - 4 kcal per gram</h1>
<p>Carbohydrate molecules include monosaccharides (glucose, fructose, galactose), disaccharides, and polysaccharides (starch).</p>
<p>Nutritionally, polysaccharides are favored over monosaccharides because they are more complex and therefore take longer to break down and
be absorbed into the bloodstream; this means that they do not cause major spikes in blood sugar levels, which are linked to heart and
vascular diseases.</p>
<h1>Proteins - 4 kcal per gram</h1>
<p>There are 20 amino acids - organic compounds found in nature that combine to form proteins. Some amino acids are essential, meaning
they need to be consumed. Other amino acids are non-essential because the body can make them.</p>
<h1>Fats - 9 kcal per gram</h1>
<p>Fats are triglycerides - three molecules of fatty acid combined with a molecule of the alcohol glycerol. Fatty acids are simple compounds
(monomers) while triglycerides are complex molecules (polymers).</p>
<p>Fats are required in the diet for health as they serve many functions, including lubricating joints, helping organs produce hormones,
assisting in absorption of certain vitamins, reducing inflammation, and preserving brain health.</p>
<h1>Macronutrients that do not provide energy</h1>
<p>These do not provide energy, but are still important:</p>
<h1>Fiber</h1>
<p>Fiber consists mostly of carbohydrates. However, because it is not easily absorbed by the body, not much of the sugars and starches
get into the blood stream. Fiber is a crucial part of nutrition, health, and fuel for gut bacteria.</p>
<h1>Water</h1>
<p>About 70 percent of the non-fat mass of the human body is water. It is vital for many processes in the human body.</p>
<p>Nobody is completely sure how much water the human body needs - claims vary from 1-7 liters per day to avoid dehydration.
We do know that water requirements are very closely linked to body size, age, environmental temperatures, physical activity,
different states of health, and dietary habits; for instance, somebody who consumes a lot of salt will require more water than
another similar person</p>
<p>Claims that 'the more water you drink, the healthier you are' are not backed with scientific evidence. The variables that influence
water requirements are so vast that accurate advice on water intake would only be valid after evaluating each person individually.</p>
<h1>Micronutrients</h1>
<p>Micronutrients are required in smaller quantities:</p>
<h1>Minerals</h1>
<p>Dietary minerals are the other chemical elements our bodies need, other than carbon, hydrogen, oxygen, and nitrogen.</p>
<p>People with a well-balanced diet will, in most cases, obtain all the minerals they need from what they eat.</p>
<p>Minerals are sometimes added to certain foods to make up for any shortages.</p>
<p>The best example of this is iodized salt - iodine is added to prevent iodine deficiency, which affects about 2 billion people, globally;
it causes mental retardation and thyroid gland problems. Iodine deficiency remains a serious public health problem in over half the planet.</p>
<p>Experts at the University of Florida say that 16 key minerals are essential for human biochemical processes:</p>
<h1>Potassium</h1>
<p>
What it does - a systemic (affects entire body) electrolyte, essential in co-regulating ATP (an important carrier of energy in cells in the
body, also key in making RNA) with sodium.Deficiency - hypokalemia - can profoundly affect the nervous system and heart.Excess -
hyperkalemia - can also profoundly affect the nervous system and heart.
</p>
<h1>Chloride</h1>
<p>What it does - key for producing stomach acid, important in the transport of molecules between cells, and vital for the proper
functioning of nerves.</p>
<p>Deficiency - hypochloremia - low salt levels, which, if severe, can be very dangerous.</p>
<p>Excess - hyperchloremia - usually no symptoms, linked with excessive fluid loss.</p>
<h1>Sodium</h1>
<p>What it does - a systemic electrolyte, and essential in regulating ATP with potassium. Important for nerve function and
regulating body fluid levels.</p>
<p>Deficiency - hyponatremia - causes cells to malfunction; extremely low sodium can be fatal.<p>
<p>Excess - hypernatremia - can also cause cells to malfunction, extremely high levels can be fatal.</p>
<h1>Calcium</h1>
<p>What it does - important for muscle, heart, and digestive health. Builds bone, assists in the synthesis and
function of blood cells.</p>
<p>Deficiency - hypocalcaemia - muscle cramps, abdominal cramps, spasms, and hyperactive deep tendon reflexes.</p>
<p>Excess - hypercalcemia - muscle weakness, constipation, undermined conduction of electrical impulses in the heart, calcium stones
in the urinary tract, impaired kidney function, and impaired absorption of iron, leading to iron deficiency.</p>
<h1>Phosphorus</h1>
<p>What it does - important for the structure of DNA, transporter of energy (ATP), component of cellular membrane,
helps strengthen bones.</p>
<p>Deficiency - hypophosphatemia, an example is rickets.</p>
<p>Excess - hyperphosphatemia, often a result of kidney failure.</p>
<h1>Magnesium</h1>
<p>What it does - processes ATP; required for good bones and management of proper muscle movement. Hundreds of enzymes
rely on magnesium to work properly.</p>
<p>Deficiency - hypomagnesemia - irritability of the nervous system with spasms of the hands and feet, muscular
twitching and cramps, constipation, and larynx spasms.</p>
<p>Excess - hypermagnesemia - nausea, vomiting, impaired breathing, low blood pressure. Very rare, but may occur
if patient has renal problems.</p>
<h1>Zinc</h1>
<p>What it does - required by many enzymes. Important for reproductive organ growth. Also important in gene expression and
regulating the nervous and immune systems.</p>
<p>Deficiency - short stature, anemia, increased pigmentation of skin, enlarged liver and spleen, impaired reproductive function,
impaired wound healing, and immune deficiency.</p>
<p>Excess - suppresses copper and iron absorption.</p>
<h1>Iron</h1>
<p>What it does - required for proteins and enzymes, especially hemoglobin, the oxygen-carrying compound in blood.</p>
<p>Deficiency - anemia.</p>
<p>Excess - iron overload disorder; iron deposits can form in organs, particularly the heart.</p>
<h1>Manganese</h1>
<p>What it does - a cofactor in enzyme functions.</p>
<p>Deficiency - wobbliness, fainting, hearing loss, weak tendons and ligaments. Less commonly, can be a cause of diabetes.</p>
<p>Excess - interferes with the absorption of dietary iron.</p>
<h1>Copper</h1>
<p>What it does - component of many enzymes.</p>
<p>Deficiency - anemia or pancytopenia (reduction in the number of red and white blood cells,
as well as platelets) and neurodegeneration.</p>
<p>Excess - can interfere with body's formation of blood cellular components; in severe cases, convulsions, palsy, and eventually death
(similar to arsenic poisoning).</p>
<h1>Iodine</h1>
<p>What it does - required for the biosynthesis of thyroxine (one type of thyroid hormone).</p>
<p>Deficiency - developmental delays, enlarged thyroid gland (in the neck), and fatigue.</p>
<p>Excess - can affect the function of the thyroid gland.</p>
<h1>Selenium</h1>
<p>What it does - essential cofactor for antioxidant enzymes.</p>
<p>Deficiency - Keshan disease - myocardial necrosis (tissue death in the heart) leading to weakening of the heart;
Kashin-Beck disease - break down of cartilage.</p>
<p>Excess - garlic-smelling breath, gastrointestinal disorders, hair loss, sloughing of nails,
fatigue, irritability, and neurological damage.</p>
<h1>Molybdenum</h1>
<p>What it does - vital part of three important enzyme systems, xanthine oxidase, aldehyde oxidase, and
sulfite oxidase. It has a vital role in uric acid formation, in carbohydrate metabolism, and sulfite detoxification.</p>
<p>Deficiency - may affect metabolism and blood counts, but as this deficiency often occurs at the same time as other
mineral deficiencies, it is hard to say which deficiency caused which health problem.</p>
<p>Excess - there is very little data on toxicity.</p>
<h1>Vitamins</h1>
<p>These are organic compounds we require in tiny amounts</p>
<p>An organic compound is any molecule that contains carbon.</p>
<p>It is called a vitamin when our bodies cannot synthesize (produce) enough or any of it, so we need to get it from our food.</p>
<p>Vitamins are classified as water soluble (they can be dissolved in water) or fat soluble (they can be dissolved in fat).
For humans, there are four fat-soluble vitamins (A, D, E, and K) and nine water-soluble vitamins (eight B vitamins and vitamin C).</p>
<p>Water-soluble vitamins need to be consumed more regularly because they are eliminated faster (in urine) and are not easily stored.</p>
<p>Fat-soluble vitamins are absorbed through the intestines with the help of fats (lipids). They are more likely to accumulate in the body
because they are harder to get rid of quickly. If too many vitamins build up, it is called hypervitaminosis. A very low-fat diet can
affect the absorption of fat-soluble vitamins.</p>
<p>We know that most vitamins have many different functions. Below is a list of vitamins, and some of their roles. Note that most often
vitamin overdose symptoms are related to supplementation or impaired metabolism or excretion, not vitamin intake from foods.</p>
<h1>Vitamin A</h1>
<p>Chemical names - retinol, retinoids, and carotenoids.</p>
<p>Solubility - fat.</p>
<p>Deficiency disease - Night-blindness.</p>
<p>Overdose disease - Keratomalacia (degeneration of the cornea).</p>
<h1>Vitamin B1</h1>
<p>Chemical name - thiamine.</p>
<p>Solubility - water.</p>
<p>Deficiency disease - beriberi, Wernicke-Korsakoff syndrome.</p>
<p>Overdose disease - rare hypersensitive reactions resembling anaphylactic shock when an overdose is due to injection.</p>
<h1>Vitamin B2</h1>
<p>Chemical name - riboflavin.</p>
<p>Solubility - water.</p>
<p>Deficiency disease - ariboflavinosis (mouth lesions, seborrhea, and vascularization of the cornea).</p>
<p>Overdose disease - no known complications. Excess is excreted in urine.</p>
<h1>Vitamin B3</h1>
<p>Chemical name - niacin.</p>
<p>Solubility - water.</p>
<p>Deficiency disease - pellagra.</p>
<p>Overdose disease - liver damage, skin problems, and gastrointestinal complaints, plus other problems.</p>
<h1>Vitamin B5</h1>
<p>Chemical name - pantothenic acid.</p>
<p>Solubility - water.</p>
<p>Deficiency disease - paresthesia (tingling, pricking, or numbness of the skin with no apparent long-term physical effect).</p>
<p>Overdose disease - none reported.</p>
<h1>Vitamin B6</h1>
<p>Chemical names - pyridoxamine, pyridoxal.</p>
<p>Solubility - water.</p>
<p>Deficiency disease - anemia, peripheral neuropathy.</p>
<p>Overdose disease - nerve damage, proprioception is impaired (the ability to sense where parts of the body are in space).</p>
<h1>Vitamin B7</h1>
<p>Chemical name - biotin.</p>
<p>Solubility - water.</p>
<p>Deficiency disease - dermatitis, enteritis.</p>
<p>Overdose disease - none reported.</p>
<h1>Vitamin B9</h1>
<p>Chemical name - folinic acid.</p>
<p>Solubility - water.</p>
<p>Deficiency disease - birth defects.</p>
<p>Overdose disease - increased risk of seizures.</p>
<h1>Vitamin B12</h1>
<p>Chemical names - cyanocobalamin, hydroxycobalamin, methylcobalamin.</p>
<p>Solubility - water.</p>
<p>Deficiency disease - megaloblastic anemia (a defect in the production of red blood cells).</p>
<p>Overdose disease - none reported.</p>
<h1>Vitamin C</h1>
<p>Chemical name - ascorbic acid.</p>
<p>Solubility - water.</p>
<p>Deficiency disease - scurvy, which can lead to a large number of complications.</p>
<p>Overdose disease - vitamin C megadose - diarrhea, nausea, skin irritation, burning upon urination,
  depletion of copper in the body, and higher risk of kidney stones.</p>
<h1>Vitamin D</h1>
<p>Chemical names - ergocalciferol, cholecalciferol.</p>
<p>Solubility - fat.</p>
<p>Deficiency disease - rickets, osteomalacia (softening of bone), recent studies indicate higher
  risk of some cancers, autoimmune disorders, and chronic diseases</p>
<p>Overdose disease - hypervitaminosis D (headache, weakness, disturbed digestion,
  increased blood pressure, and tissue calcification).</p>
<h1>Vitamin E</h1>
<p>Chemical name - tocotrienols.</p>
<p>Solubility - fat.</p>
<p>Deficiency disease - very rare, may include hemolytic anemia in newborn babies.</p>
<p>Overdose disease - dehydration, vomiting, irritability, constipation, build up of excess calcium.</p>
<h1>Vitamin K</h1>
<p>Chemical names - phylloquinone, menaquinones.</p>
<p>Solubility - fat.</p>
<p>Deficiency disease - greater tendency to bleed and bruise.</p>
<p>Overdose disease - may undermine effects of warfarin.</p>
