<p>In some cases, people don’t learn they have COPD until they’re diagnosed with lung cancer.</p>
<p>However, having COPD doesn’t necessarily mean you’ll get lung cancer. It does mean that you have a higher risk. That’s
another reason why, if you smoke, quitting is a good idea.</p>
<h1>COPD statistics</h1>
<p>Worldwide, it’s estimated that about people have moderate to severe COPD. About 12 million adults in the United States
have a diagnosis of COPD. It’s estimated that 12 million more have the disease, but don’t know it yet.</p>
<p>Most people with COPD are 40 years of age or older.</p>
<p>The majority of people with COPD are smokers or former smokers. Smoking is the most important risk factor that can be changed.
Between 20 and 30 percent of chronic smokers develop COPD that shows symptoms and signs.</p>
<p>Between 10 and 20 percent of people with COPD have never smoked. In up to of people with COPD, the cause is a genetic disorder
involving a deficiency of a protein called alpha-1-antitrypsin.</p>
