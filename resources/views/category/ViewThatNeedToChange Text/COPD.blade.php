Much of that can be attributed to an aging population.</p>
<h1>What’s the outlook for people with COPD?</h1>
<p>COPD tends to progress slowly. You may not even know you have it during the early stages.</p>
<p>Once you have a diagnosis, you’ll need to start seeing your doctor on a regular basis. You’ll
also have to take steps to manage your condition and make the appropriate changes to your daily life.</p>
<p>Early symptoms can usually be managed, and certain lifestyle choices can help you maintain a good quality of life for some time.</p>
<p>As the disease progresses, symptoms can become increasingly limiting.</p>
<p>People with severe stages of COPD may not be able to care for themselves without assistance. They are at increased risk of developing
respiratory infections, heart problems, and lung cancer. They may also be at risk of depression and anxiety.</p>
<p>COPD generally reduces life expectancy, though the outlook varies considerably from person to person. People with COPD who never smoked
may have a , while former and current smokers are likely to have a larger reduction.</p>
<p>Besides smoking, your outlook depends on how well you respond to treatment and whether you can avoid serious complications. Your doctor
is in the best position to evaluate your overall health and give you an idea about what to expect.</p>
