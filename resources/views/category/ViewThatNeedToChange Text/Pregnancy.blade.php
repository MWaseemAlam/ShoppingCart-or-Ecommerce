<h1>When to seek medical care</h1>
<p>Most women in their 20s or early 30s have a good chance of a problem-free pregnancy.
Teens and women over the age of 35 are at a higher risk for health complications.</p>
<h1>Underlying conditions</h1>
<p>Underlying health conditions such as high blood pressure, diabetes, or cardiovascular
disease will increase your risk of pregnancy complications. Other examples include:</p>
<li>cancer</li>
<li>kidney disease</li>
<li>epilepsy</li>
<p>If you have one of these conditions, ensure that it’s properly monitored and treated throughout your pregnancy.
Otherwise, it can lead to miscarriage, poor fetal growth, and birth defects.</p>
<h1>Other risk factors</h1>
<p>Other factors that can affect an otherwise healthy pregnancy include:</p>
<li>multiple-birth pregnancies, such as twins or triplets</li>
<li>infections, including STDs</li>
<li>being overweight or obese</li>
<li>anemia</li>
<h1>Pregnancy complications</h1>
<p>Pregnancy complications can involve the baby’s health, the mother’s health, or both.
They can occur during pregnancy or delivery.</p>
<p>Common pregnancy complications include:</p>
<li>high blood pressure</li>
<li>gestational diabetes</li>
<li>preeclampsia</li>
<li>preterm labor</li>
<li>miscarriage</li>
<p>Addressing them early can minimize the harms done to the mother or the baby.</p>
<h1>Pregnancy and labor</h1>
<p>Sometime after your fourth month of pregnancy, you may begin to experience Braxton-Hicks
contractions, or false labor. They’re completely normal and serve to prepare your uterus for the job ahead of real labor.</p>
<p>Braxton-Hicks contractions don’t occur at regular intervals, and they don’t increase in intensity. If you experience regular
contractions before week 37, it could be preterm labor. If this occurs, call your healthcare provider for help.</p>
<h1>Early labor</h1>
<p>Labor contractions are generally classified as early labor contractions and active labor contractions. Early labor contractions
last between 30 and 45 seconds. They may be far apart at first, but by the end of early labor, contractions will be about five
minutes apart.</p>
<p>Your water might break early during labor, or your doctor may break it for you later on during your labor. When the cervix begins
to open, you’ll see a blood-tinged discharge coating your mucous plug.</p>
<h1>Active labor</h1>
<p>In active labor, the cervix dilates, and the contractions get closer together and become more intense.</p>
<p>If you’re in active labor, you should call your healthcare provider and head to your birth setting.
If you’re unsure whether it’s active labor, it’s still a good idea to call and check in.</p>
<h1>Labor pain</h1>
<p>Pain will be at its height during active labor. Have a discussion with your doctor about
your preferred method of dealing with pain.</p>
<p>You may choose drug-free measures such as meditation, yoga, or listening to music.</p>
<p>If you choose to manage your pain with drugs, your doctor will need to know whether to use analgesics or anesthetics.</p>
<p>Analgesics, such as meperidine (Demerol), dull the pain but allow you to retain some feeling. Anesthetics, such as an
epidural, prevent certain muscle movement and completely block the pain.</p>
<h1>The bottom line</h1>
<p>Whether you’re planning for a vaginal or a cesarean delivery, you may feel nervous as your due date approaches.
Know what to expect with this guide to the different stages of labor.</p>
<h1>Prognosis</h1>
<p>You’re likely to move through each week of your pregnancy without too much trouble. Pregnancy brings with it many
changes to your body, but those changes don’t always have a serious impact on your health.</p>
<p>However, certain lifestyle choices can either help or actively harm your baby’s development.</p>
<p>Some actions that can keep you and your baby healthy include:</p>
<li>taking a multivitamin</li>
<li>getting sufficient sleep</li>
<li>practicing safe sex</li>
<li>getting a flu shot</li>
<li>visiting your dentist</li>
<p>Some things you’ll want to avoid include:</p>
<li>smoking</li>
<li>drinking alcohol</li>
<li>eating raw meat, deli meat, or unpasteurized dairy products</li>
<li>sitting in a hot tub or sauna</li>
<li>gaining too much weight</li>
<h1>Medications</h1>
<p>It can be hard to determine which medications you can take during pregnancy and which ones you should avoid. You’ll have to weigh the
benefits to your health against potential risks to the developing baby.</p>
<p>Ask your healthcare provider about any drugs you may take, even OTC ones for minor ailments such as headaches.</p>
<p>According to the Food and Drug Administration (FDA), each year 50 percent of pregnant women in the United States
report taking at least one medication.</p>
<p>In the 1970s, the FDA created a letter system to categorize drugs and their perceived risk to pregnant women. However, they began to
phase out this letter system (and use updated drug labeling) in 2015. Their new rules for drug labeling only apply to prescription drugs.</p>
<p>The service MotherToBaby also provides up-to-date information on the safety of specific drugs.</p>
<h1>The bottom line</h1>
<p>Learning or relearning all the rules of pregnancy can be overwhelming, especially if you’re having your first child.</p>
<h1>The takeaway</h1>
<p>Under the Affordable Care Act (ACA), all health insurance plans in the United States are required to offer
some level of prenatal care.</p>
<p>Once your pregnancy’s been confirmed, call your insurance provider to get an idea of what’s covered by your specific plan.
If you don’t have health insurance when you find out you’re pregnant, speak to your doctor about steps you can take to get coverage.</p>
<p>The timing of your first prenatal visit may depend on your overall health. Most women may have their first visit during week 8 of pregnancy.
Women whose pregnancies are considered high-risk, such as those who are over 35 or have chronic conditions, may be asked to see their
doctors earlier.</p>
<p>There are many ways to mentally and physically prepare for labor. Many hospitals offer birthing classes prior to delivery so that
women may better understand the signs and stages of labor.</p>
<p>In your third trimester, you may want to prepare a hospital bag of toiletries, sleepwear, and other everyday essentials. This bag would
be ready to take with you when labor begins. During the third trimester, you and your doctor should also discuss your labor and delivery
plan in detail.</p>
<p>Knowing when to go to the birth setting, who’ll be assisting in the birth, and what role your doctor will play in the process can
contribute to greater peace of mind as you enter those final weeks.</p>

