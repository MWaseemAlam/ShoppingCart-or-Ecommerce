<h1>Intrauterine devices (IUDs)</h1>
<p>Intrauterine devices (IUDs) work by mostly by stopping fertilization. They’re currently the most
effective form of birth control. The downside is that they don’t prevent sexually transmitted diseases (STDs).</p>
<h1>The pill and other hormonal birth control methods</h1>
<p>Birth control pills, patches and the vaginal ring work by controlling the hormone levels in a woman’s body.
They’re available by prescription.</p>
<p>Actions that can reduce the effectiveness of these methods include forgetting to use them as prescribed.
Effectiveness rates that mention “typical use” account for these types of human errors.</p>
<p>Other forms of hormonal birth control include the patch and the vaginal ring. They’re also available by prescription,
and their effectiveness rates are similar to those of the pill.</p>
<h1>Condoms and other barrier methods</h1>
<p>Condoms, diaphragms, and sponges are convenient and inexpensive forms of birth control that can be bought without a prescription.</p>
<p>They’re most effective when used correctly every time you have sexual intercourse. If you’re relying on these barrier methods to
avoid getting pregnant, also consider using an additional method of contraception such as spermicide or a birth control pill.</p>
<p>Other barrier methods include diaphragms and sponges. They can be bought without a prescription.</p>
<h1>Emergency contraception</h1>
<p>Several morning-after pills are available, both over the counter and by prescription. These pills aren’t intended as regular forms of
birth control. Instead, they can act as a backup if you have unprotected sex or forget to use your regular form of birth control.</p>
<p>They must be used within 120 hours (five days) of sexual contact to be effective. Some pills are most effective when taken within
72 hours (three days).</p>
<h1>Natural family planning (NFP)</h1>
<p>Natural family planning (NFP), or fertility awareness, is the birth control method with the highest failure rate. With NFP, a
woman tracks her menstrual cycle so that she can predict when she’ll ovulate. She’ll then avoid intercourse during her fertile window.</p>
<p>Accidental pregnancies can occur because there are many variables affecting a woman’s cycle from month to month.</p>
