<h1>Overview</h1>
<p>Pregnancy occurs when a sperm fertilizes an egg after it’s released from the ovary during ovulation. The fertilized egg then travels down
into the uterus, where implantation occurs. A successful implantation results in pregnancy.</p>
<p>On average, a full-term pregnancy lasts 40 weeks. There are many factors that can affect a pregnancy. Women who receive an early pregnancy
diagnosis and prenatal care are more likely to experience a healthy pregnancy and give birth to a healthy baby.</p>
<p>Knowing what to expect during the full pregnancy term is important for monitoring both your health and the health of the baby. If you’d like
to prevent pregnancy, there are also effective forms of birth control you should keep in mind.</p>
<h1>Symptoms of pregnancy</h1>
<p>You may notice some signs and symptoms before you even take a pregnancy test. Others will appear weeks later,
as your hormone levels change.</p>
<h1>Missed period</h1>
<p>A missed period is one of the earliest symptoms of pregnancy (and maybe the most classic one). However, a missed period doesn’t necessarily
mean you’re pregnant, especially if your cycle tends to be irregular.</p>
<p>There are many health conditions other than pregnancy that can cause a late or missed period.</p>
<h1>Headache</h1>
<p>Headaches are common in early pregnancy. They’re usually caused by altered hormone levels and increased blood volume. Contact your doctor
if your headaches don’t go away or are especially painful.</p>
<h1>Spotting</h1>
<p>Some women may experience light bleeding and spotting in early pregnancy. This bleeding is most often the result of implantation.
Implantation usually occurs one to two weeks after fertilization.</p>
<p>Early pregnancy bleeding can also result from relatively minor conditions such as an infection or irritation. The latter often affects
the surface of the cervix (which is very sensitive during pregnancy).</p>
<p>Bleeding can also sometimes signal a serious pregnancy complication, such as miscarriage, ectopic pregnancy, or placenta previa.
Always contact your doctor if you’re concerned.</p>
<h1>Weight gain</h1>
<p>You can expect to gain between 1 and 4 pounds in your first few months of pregnancy. Weight gain becomes more noticeable toward the
beginning of your second trimester.</p>
<h1>Pregnancy-induced hypertension</h1>
<p>High blood pressure, or hypertension, sometimes develops during pregnancy. A number of factors can increase your risk, including:</p>
<li>being overweight or obese</li>
<li>smoking</li>
<li>having a prior history or a family history of pregnancy-induced hypertension</li>
<h1>Heartburn</h1>
<p>Hormones released during pregnancy can sometimes relax the valve between your stomach and esophagus. When stomach acid leaks out,
this can result in heartburn.</p>
<h1>Constipation</h1>
<p>Hormone changes during early pregnancy can slow down your digestive system. As a result, you may become constipated.</p>
<h1>Cramps</h1>
<p>As the muscles in your uterus begin to stretch and expand, you may feel a pulling sensation that resembles menstrual cramps.
If spotting or bleeding occurs alongside your cramps, it could signal a miscarriage or an ectopic pregnancy.</p>
<h1>Back pain</h1>
<p>Hormones and stress on the muscles are the biggest causes of back pain in early pregnancy. Later on, your increased weight and
shifted center of gravity may add to your back pain. Around half of all pregnant women report back pain during their pregnancy.</p>
<h1>Anemia</h1>
<p>Pregnant women have an increased risk of anemia, which causes symptoms such as lightheadedness and dizziness.</p>
<p>The condition can lead to premature birth and low birth weight. Prenatal care usually involves screening for anemia.</p>
<h1>Depression</h1>
<p>Between 14 and 23 percent of all pregnant women develop depression during their pregnancy. The many biological and emotional
changes you experience can be contributing causes.</p>
<p>Be sure to tell your doctor if you don’t feel like your usual self.</p>
<h1>Insomnia</h1>
<p>Insomnia is another common symptom of early pregnancy. Stress, physical discomfort, and hormonal changes can be contributing causes.
A balanced diet, good sleep habits, and yoga stretches can all help you get a good night’s sleep.</p>
<h1>Breast changes</h1>
<p>Breast changes are one of the first noticeable signs of pregnancy. Even before you’re far enough along for a positive test, your
breasts may begin to feel tender, swollen, and generally heavy or full. Your nipples may also become larger and more sensitive, and the
areolae may darken.</p>
<h1>Acne</h1>
<p>Because of increased androgen hormones, many women experience acne in early pregnancy. These hormones can make your skin oilier, which can clog pores.
Pregnancy acne is usually temporary and clears up after the baby is born.</p>
<h1>Vomiting</h1>
<p>Vomiting is a component of “morning sickness,” a common symptom that usually appears within the first four months. Morning sickness is
often the first sign that you’re pregnant. Increased hormones during early pregnancy are the main cause.</p>
<h1>Hip pain</h1>
<p>Hip pain is common during pregnancy and tends to increase in late pregnancy. It can have a variety of causes, including:</p>
<li>pressure on your ligaments</li>
<li>sciatica</li>
<li>changes in your posture</li>
<li>a heavier uterus</li>
<h1>Diarrhea</h1>
<p>Diarrhea and other digestive difficulties occur frequently during pregnancy. Hormone changes, a different diet, and added
stress are all possible explanations. If diarrhea lasts more than a few days, contact your doctor to make sure you don’t become dehydrated.</p>
<h1>Stress and pregnancy</h1>
<p>While pregnancy is usually a happy time, it can also be a source of stress. A new baby means big changes to your body, your personal
relationships, and even your finances. Don’t hesitate to ask your doctor for help if you begin to feel overwhelmed.</p>
<h1>The bottom line</h1>
<p>If you think you may be pregnant, you shouldn’t rely solely on these signs and symptoms for confirmation. Taking a home pregnancy test or
seeing your doctor for lab testing can confirm a possible pregnancy.</p>
<p>Many of these signs and symptoms can also be caused by other health conditions, such as premenstrual syndrome (PMS).</p>
<h1>Pregnancy week by week</h1>
<p>Pregnancy weeks are grouped into three trimesters, each one with medical milestones for both you and the baby.</p>
<h1>First trimester</h1>
<p>A baby grows rapidly during the first trimester (weeks 1 to 12). The fetus begins developing their brain, spinal cord, and organs.
The baby’s heart will also begin to beat.</p>
<p>During the first trimester, the probability of a miscarriage is relatively high. According to the American College of Obstetricians
and Gynecologists (ACOG), it’s estimated that about 1 in 10 pregnancies end in miscarriage, and that about 85 percent of these occur
in the first trimester.</p>
<h1>Second trimester</h1>
<p>During the second trimester of pregnancy (weeks 13 to 27), your healthcare provider will likely perform an anatomy scan ultrasound.</p>
<p>This test checks the fetus’s body for any developmental abnormalities. The test results can also reveal the sex of your baby,
if you wish to find out before the baby is born.</p>
<p>You’ll probably begin to feel your baby move, kick, and punch inside of your uterus.</p>
<p>After 23 weeks, a baby in utero is considered “viable.” This means that it could survive living outside of your womb. Babies born this
early often have serious medical issues. Your baby has a much better chance of being born healthy the longer you are able to carry the
pregnancy.</p>
<h1>Third trimester</h1>
<p>During the third trimester (weeks 28 to 40), your weight gain will accelerate, and you may feel more tired.</p>
<p>Your baby can now sense light as well as open and close their eyes. Their bones are also formed.</p>
<p>As labor approaches, you may feel pelvic discomfort, and your feet may swell. Contractions that don’t lead to labor, known as
Braxton-Hicks contractions, may start to occur in the weeks before you deliver.</p>
<h1>The bottom line</h1>
<p>Every pregnancy is different, but developments will most likely occur within this general time frame.</p>
<h1>Pregnancy tests</h1>
<p>Home pregnancy tests are very accurate after the first day of your missed period. If you get a positive result on a home pregnancy test,
you should schedule an appointment with your doctor right away. An ultrasound will be used to confirm and date your pregnancy.</p>
<p>Pregnancy is diagnosed by measuring the body’s levels of human chorionic gonadotropin (hCG). Also referred to as the pregnancy hormone,
hCG is produced upon implantation. However, it may not be detected until after you miss a period.</p>
<p>After you miss a period, hCG levels increase rapidly. hCG is detected through either a urine or a blood test.</p>
<p>Urine tests may be provided at a doctor’s office, and they’re the same as the tests you can take at home.</p>
<p>Blood tests can be performed in a laboratory. hCG blood tests are about as accurate as home pregnancy tests.
The difference is that blood tests may be ordered as soon as six days after ovulation.</p>
<p>The sooner you can confirm you’re pregnant, the better. An early diagnosis will allow you to take better care of your baby’s health.</p>
<h1>Pregnancy and vaginal discharge</h1>
<p>An increase in vaginal discharge is one of the earliest signs of pregnancy. Your production of discharge may increase as early as one to
two weeks after conception, before you’ve even missed a period.</p>
<p>As your pregnancy progresses, you’ll continue to produce increasing amounts of discharge. The discharge will also tend to become thicker
and occur more frequently. It’s usually heaviest at the end of your pregnancy.</p>
<p>During the final weeks of your pregnancy, your discharge may contain streaks of thick mucus and blood. This is called “the bloody show.”
It can be an early sign of labor. You should let your doctor know if you have any bleeding.</p>
<p>Normal vaginal discharge, or leukorrhea, is thin and either clear or milky white. It’s also mild-smelling.</p>
<p>If your discharge is yellow, green, or gray with a strong, unpleasant odor, it’s considered abnormal. Abnormal discharge can be a sign of
an infection or a problem with your pregnancy, especially if there’s redness, itching, or vulvar swelling.</p>
<h1>Pregnancy and urinary tract infections (UTIs)</h1>
<p>Urinary tract infections (UTIs) are one of the most common complications women experience during pregnancy. Bacteria can get inside
a woman’s urethra, or urinary tract, and can move up into the bladder. The fetus puts added pressure on the bladder, which can cause
the bacteria to be trapped, causing an infection.</p>
<p>Symptoms of a UTI usually include pain and burning or frequent urination. You may also experience:</p>
<li>cloudy or blood-tinged urine</li>
<li>pelvic pain</li>
<li>lower back pain</li>
<li>fever</li>
<li>nausea and vomiting</li>
<p>Nearly 18 percent of pregnant women develop a UTI. You can help prevent these infections by emptying your bladder frequently,
especially before and after sex. Drink plenty of water to stay hydrated. Avoid using douches and harsh soaps in the genital area.</p>
<p>Contact your healthcare provider if you have symptoms of a UTI. Infections during pregnancy can be dangerous because they increase
the risk of premature labor.</p>
<p>When caught early, most UTIs can be treated with antibiotics that are effective against bacteria but still safe for use during pregnancy.
Follow the advice here to prevent UTIs before they even start.</p>
<h1>Pregnancy prevention</h1>
<p>Women who have male sexual partners should consider birth control if they’re not interested in becoming pregnant.</p>
<p>Some methods of pregnancy prevention work better for certain individuals. Talk to your doctor about birth control that’s right for you.
A few of the most common birth control methods are discussed below:</p>
