<h1>Who gets heart disease?</h1>
<p>Heart disease describes a range of conditions that affect your heart. Diseases under the heart disease umbrella include blood vessel
diseases, such as coronary artery disease; heart rhythm problems (arrhythmias); and heart defects you're born with (congenital heart
defects), among others.
</p>
<h1>What are the different types of heart disease?</h1>
<p>Heart disease encompasses a wide range of cardiovascular problems. Several diseases and conditions fall under the umbrella of
heart disease.Types of heart disease include:</p>
<li><b>Arrhythmia.</b> An arrhythmia is a heart rhythm abnormality.</li>
<li><b>Atherosclerosis</b> is a condition that develops when a substance called plaque builds up in the walls of the arteries. This buildup
narrows the arteries, making it harder for blood to flow through. If a blood clot forms, it can block the blood flow. This can cause a
heart attack or stroke.</li>
<li>Cardiomyopathy. This condition causes the heart’s muscles to harden or grow weak.</li>
<li>Coronary artery disease (CAD). CAD is caused by the buildup of plaque in the heart’s arteries. It’s sometimes called ischemic heart disease.</li>
<li>Heart infections. Heart infections may be caused by bacteria, viruses, or parasites.</li>
<h1>What are the symptoms of heart disease?</h1>
<p>Different types of heart disease may result in a variety of different symptoms.</p>
<h1>Arrhythmias</h1>
<p>Arrhythmias are abnormal heart rhythms. The symptoms you experience may depend on the type of arrhythmia you have — heartbeats that are
too fast or too slow. Symptoms of an arrhythmia include:</p>
<li>lightheadedness</li>
<li>fluttering heart or racing heartbeat</li>
<li>slow pulse</li>
<li>fainting spells</li>
<li>dizziness</li>
<li>chest pain</li>

<h1>Atherosclerosis</h1>
<p>Atherosclerosis reduces blood supply to your extremities. In addition to chest pain and shortness of breath, symptoms of atherosclerosis
include:</p>
<li>coldness, especially in the limbs</li>
<li>numbness, especially in the limbs</li>
<li>unusual or unexplained pain</li>
<li>weakness in your legs and arms</li>
<h1>Symptoms:</h1>
<p>Heart disease symptoms depend on what type of heart disease you have.</p>
<p>Cardiovascular disease symptoms may be different for men and women. For instance, men are more likely to have chest pain; women are more
likely to have other symptoms along with chest discomfort, such as shortness of breath, nausea and extreme fatigue.</p>
<h1>Symptoms of Atherosclerotic disease:</h1>
<p>Symptoms can include:</p>
<li>Chest pain, chest tightness, chest pressure and chest discomfort (angina)</li>
<li>Shortness of breath</li>
<li>Pain, numbness, weakness or coldness in your legs or arms if the blood vessels in those parts of your body are narrowed</li>
<li>Pain in the neck, jaw, throat, upper abdomen or back</li>
<p>You might not be diagnosed with cardiovascular disease until you have a heart attack, angina, stroke or heart failure. It's important to
watch for cardiovascular symptoms and discuss concerns with your doctor. Cardiovascular disease can sometimes be found early with regular
evaluations.</p>
<h1>Symptoms of Arrhythmias disease:</h1>
<p>A heart arrhythmia is an abnormal heartbeat. Your heart may beat too quickly, too slowly or irregularly. Heart arrhythmia symptoms can
include:</p>
<li>Fluttering in your chest</li>
<li>Racing heartbeat (tachycardia)</li>
<li>Slow heartbeat (bradycardia)</li>
<li>Chest pain or discomfort</li>
<li>Shortness of breath</li>
<li>Lightheadedness</li>
<li>Dizziness</li>
<li>Fainting (syncope) or near fainting</li>
<h1>Symptoms of heart defects disease:</h1>
<p>Serious congenital heart defects — defects you're born with — usually become evident soon after birth. Heart defect symptoms in children
could include:</p>
<li>Pale gray or blue skin color (cyanosis)</li>
<li>Swelling in the legs, abdomen or areas around the eyes</li>
<li>In an infant, shortness of breath during feedings, leading to poor weight gain</li>
<p>Less serious congenital heart defects are often not diagnosed until later in childhood or during adulthood. Signs and symptoms of congenital
heart defects that usually aren't immediately life-threatening include:</p>
<li>Easily getting short of breath during exercise or activity</li>
<li>Easily tiring during exercise or activity</li>
<li>Swelling in the hands, ankles or feet</li>
<h1>Symptoms of dilated cardiomyopathy disease:</h1>
<p>In early stages of cardiomyopathy, you may have no symptoms. As the condition worsens, symptoms may include:</p>
<li>Breathlessness with exertion or at rest</li>
<li>Swelling of the legs, ankles and feet</li>
<li>Fatigue</li>
<li>Irregular heartbeats that feel rapid, pounding or fluttering</li>
<li>Dizziness, lightheadedness and fainting</li>
<h1>Symptoms of valvular heart disease:</h1>
<p>The heart has four valves — the aortic, mitral, pulmonary and tricuspid valves — that open and close to direct blood flow through your
heart. Valves may be damaged by a variety of conditions leading to narrowing (stenosis), leaking (regurgitation or insufficiency) or
improper closing (prolapse).</p>
<p>Depending on which valve isn't working properly, valvular heart disease symptoms generally include:</p>
<li>Fatigue</li>
<li>Shortness of breath</li>
<li>Irregular heartbeat</li>
<li>Swollen feet or ankles</li>
<li>Chest pain</li>
<li>Fainting (syncope)</li>
<h1>When to see a doctor</h1>
<p>Seek emergency medical care if you have these heart disease symptoms:</p>
<li>Chest pain</li>
<li>Shortness of breath</li>
<li>Fainting</li>
<p>Heart disease is easier to treat when detected early, so talk to your doctor about your concerns regarding your heart health. If you're
concerned about developing heart disease, talk to your doctor about steps you can take to reduce your heart disease risk. This is especially
important if you have a family history of heart disease.</p>
<h3>Causes</h3>
<h1>How the heart works</h1>
<p>Your heart is a pump. It's a muscular organ about the size of your fist, situated slightly left of center in your chest. Your heart is
divided into the right and the left side. The division prevents oxygen-rich blood from mixing with oxygen-poor blood. Oxygen-poor blood
returns to the heart after circulating through your body.</p>
<li>The right side of the heart, comprising the right atrium and ventricle, collects and pumps blood to the lungs through the pulmonary
arteries.</li>
<li>The lungs refresh the blood with a new supply of oxygen. The lungs also breathe out carbon dioxide, a waste product.</li>
<li>Oxygen-rich blood then enters the left side of the heart, comprising the left atrium and ventricle.</li>
<li>The left side of the heart pumps blood through the aorta to supply tissues throughout the body with oxygen and nutrients.</li>
<h1>Heart valves</h1>
<p>Four valves within your heart keep your blood moving the right way by opening only one way and only when they need to. To function
properly, the valve must be formed properly, must open all the way and must close tightly so there's no leakage. The four valves are:</p>
<li>Tricuspid</li>
<li>Mitral</li>
<li>Pulmonary</li>
<li>Aortic</li>
<h1>Heartbeats</h1>
<p>A beating heart contracts and relaxes in a continuous cycle.</p>
<li>During contraction (systole), your ventricles contract, forcing blood into the vessels to your lungs and body.</li>
<li>During relaxation (diastole), the ventricles are filled with blood coming from the upper chambers (left and right atria).</li>
<h1>Electrical system</h1>
<p>Your heart's electrical wiring keeps it beating, which controls the continuous exchange of oxygen-rich blood with oxygen-poor blood.
This exchange keeps you alive.</p>
<li>Electrical impulses begin high in the right atrium and travel through specialized pathways to the ventricles, delivering the signal for
the heart to pump.</li>
<li>The conduction system keeps your heart beating in a coordinated and normal rhythm, which keeps blood circulating.</li>
<h1>Causes of Heart Diseases.</h1>
<p>The causes of heart disease vary by type of heart disease.</p>
<h1>Causes of cardiovascular disease:</h1>
<p>While cardiovascular disease can refer to different heart or blood vessel problems, the term is often used to mean damage to your heart or
blood vessels by atherosclerosis (ath-ur-o-skluh-ROE-sis), a buildup of fatty plaques in your arteries. Plaque buildup thickens and
stiffens artery walls, which can inhibit blood flow through your arteries to your organs and tissues.</p>
<p>Atherosclerosis is also the most common cause of cardiovascular disease. It can be caused by correctable problems, such as an unhealthy
diet, lack of exercise, being overweight and smoking.</p>
<h1>Causes of heart arrhythmia</h1>
<p>Common causes of abnormal heart rhythms (arrhythmias) or conditions that can lead to arrhythmias include:</p>
<li>Heart defects you're born with (congenital heart defects)</li>
<li>Coronary artery disease</li>
<li>High blood pressure</li>
<li>Diabetes</li>
<li>Smoking</li>
<li>Excessive use of alcohol or caffeine</li>
<li>Drug abuse</li>
<li>Stress</li>
<li>Some over-the-counter medications, prescription medications, dietary supplements and herbal remedies</li>
<li>Valvular heart disease</li>
<p>In a healthy person with a normal, healthy heart, it's unlikely for a fatal arrhythmia to develop without some outside trigger, such as
an electrical shock or the use of illegal drugs. That's primarily because a healthy person's heart is free from any abnormal conditions
that cause an arrhythmia, such as an area of scarred tissue.</p>
<p>However, in a heart that's diseased or deformed, the heart's electrical impulses may not properly start or travel through the heart,
making arrhythmias more likely to develop.</p>
<h1>Causes of congenital heart defects</h1>
<p>Congenital heart defects usually develop while a baby is in the womb. Heart defects can develop as the heart develops, about a month
after conception, changing the flow of blood in the heart. Some medical conditions, medications and genes may play a role in causing heart
defects.</p>
<p>Heart defects can also develop in adults. As you age, your heart's structure can change, causing a heart defect.</p>
<h1>Causes of cardiomyopathy</h1>
<p>The cause of cardiomyopathy, a thickening or enlarging of the heart muscle, may depend on the type:</p>
<li><b>Dilated cardiomyopathy.</b> The cause of this most common type of cardiomyopathy often is unknown. It may be caused by reduced blood flow to
the heart (ischemic heart disease) resulting from damage after a heart attack, infections, toxins and certain drugs. It may also be
inherited from a parent. It usually enlarges (dilates) the left ventricle.</li>
<li><b>Hypertrophic cardiomyopathy.</b> This type, in which the heart muscle becomes abnormally thick, usually is inherited. It can also develop
over time because of high blood pressure or aging.</li>
<li><b>Restrictive cardiomyopathy.</b> This least common type of cardiomyopathy, which causes the heart muscle to become rigid and less elastic, can
occur for no known reason. Or it may be caused by diseases, such as connective tissue disorders, excessive iron buildup in your body
(hemochromatosis), the buildup of abnormal proteins (amyloidosis) or by some cancer treatments.</li>
<h1>Causes of heart infection</h1>
<p>A heart infection, such as endocarditis, is caused when an irritant, such as a bacterium, virus or chemical, reaches your heart muscle.
The most common causes of heart infection include:</p>
<li>Bacteria</li>
<li>Viruses</li>
<li>Parasites</li>
<h1>Causes of valvular heart disease</h1>
<p>There are many causes of diseases of your heart valves. You may be born with valvular disease, or the valves may be damaged by
conditions such as:</p>
<li>Rheumatic fever</li>
<li>Infections (infectious endocarditis)</li>
<li>Connective tissue disorders</li>
<h1>Risk factors</h1>
<p>Risk factors for developing heart disease include:</p>
<li>Age. Aging increases your risk of damaged and narrowed arteries and weakened or thickened heart muscle.</li>
<li>Sex. Men are generally at greater risk of heart disease. However, women's risk increases after menopause.</li>
<li>Family history. A family history of heart disease increases your risk of coronary artery disease, especially if a parent developed it at
an early age (before age 55 for a male relative, such as your brother or father, and 65 for a female relative, such as your mother or
sister).</li>
<li>Smoking. Nicotine constricts your blood vessels, and carbon monoxide can damage their inner lining, making them more susceptible to
atherosclerosis. Heart attacks are more common in smokers than in nonsmokers.</li>
<li>Certain chemotherapy drugs and radiation therapy for cancer. Some chemotherapy drugs and radiation therapies may increase the risk of
cardiovascular disease.</li>
<li>Poor diet. A diet that's high in fat, salt, sugar and cholesterol can contribute to the development of heart disease.</li>
<li>High blood pressure. Uncontrolled high blood pressure can result in hardening and thickening of your arteries, narrowing
the vessels through which blood flows.</li>
<li>High blood cholesterol levels. High levels of cholesterol in your blood can increase the risk of formation of plaques
and atherosclerosis.</li>
<li>Diabetes. Diabetes increases your risk of heart disease. Both conditions share similar risk factors, such as obesity and
high blood pressure.</li>
<li>Obesity. Excess weight typically worsens other risk factors.</li>
<li>Physical inactivity. Lack of exercise also is associated with many forms of heart disease and some of its other risk factors, as well.</li>
<li>Stress. Unrelieved stress may damage your arteries and worsen other risk factors for heart disease.</li>
<li>Poor hygiene. Not regularly washing your hands and not establishing other habits that can help prevent viral or bacterial infections can
put you at risk of heart infections, especially if you already have an underlying heart condition. Poor dental health also may contribute
to heart disease.</li>
<h1>Complications</h1>
<p>Complications of heart disease include:</p>
<li>Heart failure. One of the most common complications of heart disease, heart failure occurs when your heart can't pump enough blood to
meet your body's needs. Heart failure can result from many forms of heart disease, including heart defects, cardiovascular disease, valvular
heart disease, heart infections or cardiomyopathy.</li>
<li>Heart attack. A blood clot blocking the blood flow through a blood vessel that feeds the heart causes a heart attack, possibly damaging
or destroying a part of the heart muscle. Atherosclerosis can cause a heart attack.</li>
<li>Stroke. The risk factors that lead to cardiovascular disease also can lead to an ischemic stroke, which happens when the arteries to your
brain are narrowed or blocked so that too little blood reaches your brain. A stroke is a medical emergency — brain tissue begins to die
within just a few minutes of a stroke.</li>
<li>Aneurysm. A serious complication that can occur anywhere in your body, an aneurysm is a bulge in the wall of your artery. If an aneurysm
bursts, you may face life-threatening internal bleeding.</li>
<li>Peripheral artery disease. Atherosclerosis also can lead to peripheral artery disease. When you develop peripheral artery disease, your
extremities — usually your legs — don't receive enough blood flow. This causes symptoms, most notably leg pain when walking (claudication).</li>
<li>Sudden cardiac arrest. Sudden cardiac arrest is the sudden, unexpected loss of heart function, breathing and consciousness, often caused
by an arrhythmia. Sudden cardiac arrest is a medical emergency. If not treated immediately, it is fatal, resulting in sudden cardiac death.</li>
<h1>Prevention</h1>
<p>Certain types of heart disease, such as heart defects, can't be prevented. However, you can help prevent many other types of heart disease
by making the same lifestyle changes that can improve your heart disease, such as:</p>
<li>Quit smoking</li>
<li>Control other health conditions, such as high blood pressure, high cholesterol and diabetes</li>
<li>Exercise at least 30 minutes a day on most days of the week</li>
<li>Eat a diet that's low in salt and saturated fat</li>
<li>Maintain a healthy weight</li>
<li>Reduce and manage stress</li>
<li>Practice good hygiene</li>
