<h1>What is depression?</h1>
<p>Depression is classified as a mood disorder. It may be described as feelings of sadness, loss, or anger that interfere
with a person’s everyday activities.</p>
<p>It’s also fairly common. The estimates that 8.1 percent of American adults ages 20 and over had depression in any given
two-week period from 2013 to 2016.</p>
<p>People experience depression in different ways. It may interfere with your daily work, resulting in lost time and lower
productivity. It also can influence relationships and some chronic health conditions.</p>
<p>Conditions that can get worse due to depression include:</p>
<li>arthritis</li>
<li>asthma</li>
<li>cardiovascular disease</li>
<li>cancer</li>
<li>diabetes</li>
<li>obesity</li>
<p>It’s important to realize that feeling down at times is a normal part of life. Sad and upsetting events happen to everyone.
  But, if you’re feeling miserable or hopeless on a regular basis, you could be dealing with depression.</p>
<p>Depression is considered a serious medical condition, and it can get worse without proper treatment. Yet, those who seek treatment
often see improvements in symptoms in just a few weeks.</p>
<h1>Depression symptoms</h1>
<p>Depression can be more than a constant state of sadness or feeling “blue.”</p>
<p>Major depression can cause a variety of symptoms. Some affect your mood, and
others affect your body. Symptoms may also be ongoing or come and go.</p>
<p>Depression can affect men, women, and children differently.</p>
<p>Symptoms of depression in men may include:</p>
<li><b>Mood: </b>anger, aggressiveness, irritability, anxiousness, restlessness</li>
<li><b>Emotional:</b> feeling empty, sad, hopeless</li>
<li><b>Behavioral:</b> loss of interest, no longer finding pleasure in favorite activities, feeling tired easily, thoughts of suicide,
  drinking excessively, using drugs, engaging in high-risk activities</li>
<li><b>Sexual:</b> reduced sexual desire, lack of sexual performance</li>
<li><b>Cognitive:</b> inability to concentrate, difficulty completing tasks, delayed responses during conversations</li>
<li><b>Sleep:</b> insomnia, restless sleep, excessive sleepiness, not sleeping through the night</li>
<li><b>Physical:</b> fatigue, pains, headache, digestive problems</li>
<p>Symptoms of depression in women may include:</p>
<li><b>Mood:</b> irritability</li>
<li><b>Emotional:</b> feeling sad or empty, anxious or hopeless</li>
<li><b>Behavioral:</b> loss of interest in activities, withdrawing from social engagements, thoughts of suicide</li>
<li><b>Cognitive:</b> thinking or talking more slowly</li>
<li><b>Sleep:</b> difficulty sleeping through the night, waking early, sleeping too much</li>
<li><b>Physical:</b> decreased energy, greater fatigue, changes in appetite, weight changes, aches, pain, headaches, increased cramps</li>
<p>Symptoms of depression in children may include:</p>
<li><b>Mood:</b> irritability, anger, mood swings, crying</li>
<li><b>Emotional:</b> feelings of incompetence (e.g. “I can’t do anything right”) or despair, crying, intense sadness</li>
<li><b>Behavioral:</b> getting into trouble at school or refusing to go to school, avoiding friends or siblings, thoughts of death or suicide</li>
<li><b>Cognitive:</b> difficulty concentrating, decline in school performance, changes in grades</li>
<li><b>Sleep:</b> difficulty sleeping or sleeping too much</li>
<li><b>Physical:</b> loss of energy, digestive problems, changes in appetite, weight loss or gain</li>
<p>The symptoms can extend beyond your mind. These eight physical symptoms of depression prove that depression
isn’t just all in your head.</p>
<h1>Depression causes</h1>
<p>There are several possible causes of depression. They can range from biological to circumstantial.</p>
<p>Common causes include:</p>
<li><b>Family history.</b> You’re at a higher risk for developing depression if you have a family history of depression or another
mood disorder.</li>
<li><b>Early childhood trauma.</b> Some events impact the way that body reacts to fear and stressful situations.</li>
<li><b>Brain structure.</b> There’s a greater risk for depression if the frontal lobe of your brain is less active. However,
  scientists don’t know if this happens before or after the onset of depressive symptoms.</li>
<li><b>Medical conditions.</b> Certain conditions may put you at higher risk, such as chronic illness, insomnia, chronic pain, or
attention-deficit hyperactivity disorder (ADHD).</li>
<li><b>Drug use.</b> A history of drug or alcohol misuse can impact your risk.</li>
<p>Many other people may never learn the cause of their depression.</p>
<p>About 30 percent of people who have a substance use problem also experience depression. In addition to these causes,
  other risk factors for depression include:</p>
<li>low self-esteem or being self-critical</li>
<li>personal history of mental illness</li>
<li>certain medications</li>
<li>stressful events, such as loss of a loved one, economic problems, or a divorce</li>
<h1>Depression test</h1>
<p>There isn’t a single test to diagnose depression. But your doctor can make a diagnosis
based on your symptoms and a psychological evaluation.</p>
<p>In most cases, your doctor will ask a series of questions about your moods, appetite, sleep pattern, activity level, and thoughts.</p>
<p>Because depression can be linked to other health problems, your doctor may also conduct a physical examination and order blood work.
Sometimes thyroid problems or a vitamin D deficiency can trigger symptoms of depression.</p>
<p>Don’t ignore symptoms of depression. If your mood doesn’t improve or gets worse, seek medical help. Depression is a
serious mental illness with risks of complications.</p>
<p>If left untreated, complications include:</p>
<li>weight gain or loss</li>
<li>physical pain</li>
<li>substance use problems</li>
<li>panic attacks</li>
<li>relationship problems</li>
<li>social isolation</li>
<li>suicidal thoughts</li>
<li>self-mutilation</li>
<h1>Types of depression</h1>
<p>Depression can be broken into categories depending on the severity of symptoms. Some people experience mild and temporary episodes,
while others experience severe and ongoing depressive episodes.</p>
<p>There are two main types: major depressive disorder and persistent depressive disorder.</p>
<h1>Major depressive disorder</h1>
<p>Major depressive disorder is the more severe form of depression. It’s characterized by persistent feelings of sadness, hopelessness,
 and worthlessness that don’t go away on their own.</p>
<p>In order to be diagnosed with clinical depression, you must experience 5 or more of the following symptoms over a two-week period:</p>
<li>feeling depressed most of the day</li>
<li>loss of interest in most regular activities</li>
<li>significant weight loss or gain</li>
<li>sleeping too much or not being able to sleep</li>
<li>slowed thinking or movement</li>
<li>fatigue or low energy most days</li>
<li>feelings of worthlessness or guilt</li>
<li>loss of concentration or indecisiveness</li>
<li>recurring thoughts of death or suicide</li>
<p>There are different subtypes of major depressive disorder (which the American Psychiatric Association refers to as “specifiers”).
These include:</p>
<li>atypical features</li>
<li>anxious distress</li>
<li>mixed features</li>
<li>peripartum onset, during pregnancy or right after giving birth</li>
<li>seasonal patterns</li>
<li>melancholic features</li>
<li>psychotic features</li>
<li>catatonia</li>
<h1>Persistent depressive disorder</h1>
<p>Persistent depressive disorder (PDD) used to be called dysthymia. It’s a milder, but chronic, form of depression.</p>
<p>In order for the diagnosis to be made, symptoms must last for at least two years. PDD can affect your life more
than major depression because it lasts for a longer period.</p>
<p>It’s common for people with PDD to:</p>
<li>lose interest in normal daily activities</li>
<li>feel hopeless</li>
<li>lack productivity</li>
<li>have low self-esteem</li>
<h1>Treatment for depression</h1>
<p>Living with depression can be difficult, but treatment can help improve your quality of life.
Talk to your doctor about possible options.</p>
<p>You may successfully manage symptoms with one form of treatment, or you may find that a
combination of treatments works best. It’s common to combine medical treatments and lifestyle therapies, including the following:</p>
<h1>Medications</h1>
<p>Your doctor may prescribe antidepressants, antianxiety, or antipsychotic medications.</p>
<p>Each type of medication that’s used to treat depression has benefits and potential risks.</p>
<h1>Psychotherapy</h1>
<p>Speaking with a therapist can help you learn skills to cope with negative feelings.
You may also benefit from family or group therapy sessions.</p>
<h1>Light therapy</h1>
<p>Exposure to doses of white light can help regulate mood and improve symptoms of depression.
This therapy is commonly used in seasonal affective disorder (which is now called major depressive disorder with seasonal pattern).</p>
<h1>Alternative therapies</h1>
<p>Ask your doctor about acupuncture or meditation. Some herbal supplements are also used to treat depression,
like St. John’s wort, SAMe, and fish oil.</p>
<p>Talk with your doctor before taking a supplement or combining a supplement with prescription medication because some supplements can react
with certain medications. Some supplements may also worsen depression or reduce the effectiveness of medication.</p>
<h1>Exercise</h1>
<p>Aim for 30 minutes of physical activity three to five days a week. Exercise can increase your body’s production of endorphins,
which are hormones that improve your mood.</p>
<h1>Avoid alcohol and drugs</h1>
<p>Drinking or using drugs may make you feel better for a little bit. But in the long run, these substances can make depression
  and anxiety symptoms worse.</p>
<h1>Natural treatment for depression</h1>
<p>Traditional depression treatment uses a combination of prescription medication and counseling. But there are also
  alternative or complementary treatments you can try.</p>
<p>It’s important to remember that many of these natural treatments have few studies showing their effects on depression, good or bad.
Likewise, the U.S. Food and Drug Administration (FDA) doesn’t approve many of the dietary supplements on the market in the United States,
so you want to make sure you’re buying products from a trustworthy brand.</p>
<p>Talk to your doctor before adding supplements to your treatment plan.</p>
<h1>Supplements</h1>
<p>Several types of supplements are thought to have some positive impact on depression symptoms.</p>
<h1>St. John’s wort</h1>
<p>Studies are mixed, but this natural treatment is used in Europe as an antidepressant medication. In the United States,
it hasn’t received the same approval.</p>
<h1>S-adenosyl-L-methionine (SAMe)</h1>
<p>This compound has shown in limited studies to possibly ease symptoms of depression. The effects were best seen in people
taking selective serotonin reuptake inhibitors (SSRIs), a type of traditional antidepressant.</p>
<h1>5-hydroxytryptophan (5-HTP)</h1>
<p>5-HTP may raise serotonin levels in the brain, which could ease symptoms. Your body makes this chemical when you consume
tryptophan, a protein building block.</p>
<h1>Omega-3 fatty acids</h1>
<p>These essential fats are important to neurological development and brain health. Adding omega-3 supplements to your
diet may help reduce depression symptoms.</p>
<h1>Essential oils</h1>
<p>Essential oils are a popular natural remedy for many conditions, but research into their effects on depression is limited.</p>
<p>People with depression may find symptom relief with the following essential oils:</p>
<li><b>Wild ginger:</b> Inhaling this strong scent may activate serotonin receptors in your brain. This may slow the release
of stress-inducing hormones.</li>
<li><b>Bergamot:</b> This citrusy essential oil has been shown to reduce anxiety in patients awaiting surgery. The same benefit may help
individuals who experience anxiety as a result of depression, but there’s no research to support that claim.</li>
<p>Other oils, such as chamomile or rose oil, may have a calming effect when they’re inhaled. Those oils may be
beneficial during short-term use.</p>
<h1>Vitamins</h1>
<p>Vitamins are important to many bodily functions. Research suggests two vitamins are especially useful for easing
symptoms of depression:</p>
<li><b>Vitamin B:</b> B-12 and B-6 are vital to brain health. When your vitamin B levels are low, your risk for developing
depression may be higher.</li>
<li><b>Vitamin D:</b> Sometimes called the sunshine vitamin because exposure to the sun supplies it to your body, Vitamin D is important
for brain, heart, and bone health. People who are depressed are more likely to have low levels of this vitamin.</li>
<p>Many herbs, supplements, and vitamins claim to help ease symptoms of depression, but most haven’t shown themselves
to be effective in clinical research.</p>
<h1>Preventing depression</h1>
<p>Depression isn’t generally considered to be preventable. It’s hard to recognize what causes it,
which means preventing it is more difficult.</p>
<p>But once you’ve experienced a depressive episode, you may be better prepared to prevent a future episode by
learning which lifestyle changes and treatments are helpful.</p>
<p>Techniques that may help include:</p>
<li>regular exercise</li>
<li>getting plenty of sleep</li>
<li>maintaining treatments</li>
<li>reducing stress</li>
<li>building strong relationships with others</li>
<h1>Bipolar depression</h1>
<p>Bipolar depression occurs in certain types of bipolar disorder, when the person experiences a depressive episode.</p>
<p>People with bipolar disorder may experience significant mood swings. Episodes in bipolar 2, for instance, typically
range from manic episodes of high energy to depressive episodes of low energy.</p>
<p>This depends on the type of bipolar disorder you have. A diagnosis of bipolar 1 only has to have
the presence of manic episodes, not depression.</p>
<p>Symptoms of depression in people with bipolar disorder may include:</p>
<li>loss of interest or enjoyment from normal activities</li>
<li>feeling sad, worried, anxious, or empty</li>
<li>not having energy or struggling to complete tasks</li>
<li>difficulty with recall or memory</li>
<li>sleeping too much or insomnia</li>
<li>weight gain or weight loss as a result of increased or decreased appetite</li>
<li>contemplating death or suicide</li>
<p>If bipolar disorder is treated, many will experience fewer and less severe symptoms of depression, if they experience depressive
episodes. These 7 treatments may help ease symptoms of bipolar depression.</p>
<h1>Depression and anxiety</h1>
<p>Depression and anxiety can occur in one person at the same time. In fact, studies have shown that of people with depressive
disorders also have anxiety symptoms.</p>
<p>Though they’re thought to be caused by different things, depression and anxiety can produce several similar symptoms. These include irritability,
difficulty with memory or concentration, and sleep problems.</p>
<p>The two conditions also share some common treatments. Both anxiety and depression can be treated with therapy, like cognitive behavioral therapy,
medication, or alternative therapies, including hypnotherapy.</p>
<p>If you think you’re experiencing symptoms of either or both conditions, make an appointment to talk with your doctor. You can work
with your doctor to identify coexisting symptoms of anxiety and depression and how they can be treated.</p>
<h1>Depression and obsessive-compulsive disorder (OCD)</h1>
<p>Obsessive-compulsive disorder (OCD) is a type of anxiety disorder. It causes unwanted and repeated thoughts,
urges, and fears (obsessions).</p>
<p>These fears cause you to act out repeated behaviors or rituals (compulsions) that you hope will ease the
stress caused by the obsessions.</p>
<p>People diagnosed with OCD frequently find themselves in a loop of obsessions and compulsions. If you have these behaviors, you may feel
isolated because of them. This can lead to withdrawal from friends and social situations, which can increase your risk for depression.</p>
<p>It’s not uncommon for someone with OCD to also have depression. Having one anxiety disorder can increase your odds for having another. Up
to of people with OCD also have major depression.</p>
<p>This dual diagnosis is a concern with children, too. Their compulsive behaviors, which may be first developing at a young age, can make
them feel unusual. That can lead to withdrawing from friends, and that increases the child’s risk for depression.</p>
<h1>Depression with psychosis</h1>
<p>Some individuals who have been diagnosed with major depression may also have symptoms of another mental disorder, psychosis. When the
two conditions occur together, it’s known as depressive psychosis.</p>
<p>Depressive psychosis causes people to see, hear, believe, or smell things that aren’t real. People with the condition may also experience
feelings of sadness, hopelessness, and irritability.</p>
<p>The combination of the two conditions is particularly dangerous. That’s because someone with depressive psychosis may experience delusions
that lead them to be suicidal or take unusual risks.</p>
<p>It’s unclear what causes these two conditions or why they can occur together. But treatment can successfully ease symptoms. Treatments
include medications and electroconvulsive therapy.</p>
<p>Understanding the risk factors and possible causes can help you be aware of early symptoms. </p>
<h1>Depression in pregnancy</h1>
<p>Pregnancy is often an exciting time for people. But it’s still common for an expecting mother to experience depression.</p>
<p>Symptoms of depression during pregnancy include:</p>
<li>changes in appetite or eating habits</li>
<li>feeling hopeless</li>
<li>anxiety</li>
<li>losing interest in activities and things you previously enjoyed</li>
<li>persistent sadness</li>
<li>troubles concentrating or remembering</li>
<li>sleep problems, including insomnia or sleeping too much</li>
<li>thoughts of death or suicide</li>
<p>Treatment for depression during pregnancy may focus entirely on talk therapy and other natural treatments.</p>
<p>While some women do take antidepressants during their pregnancy, it’s not clear which are the safest. Your
doctor may encourage you to try an alternative option until after the baby is delivered.</p>
<p>The risks for depression don’t end once the baby arrives. Postpartum depression (currently called major
depressive disorder with peripartum onset) is a serious concern for new mothers.</p>
<p>Recognizing the symptoms may help you spot a problem and seek help before it becomes overwhelming.</p>
<h1>Depression and alcohol</h1>
<p>Research has established a link between alcohol use and depression. People who have depression are more
  likely to misuse alcohol.</p>
<p>Out of the 20.2 million U.S. adults who experienced a substance use disorder, about 50 percent had a cooccurring mental illness.</p>
<p>According to a 2012 study, of people who are alcohol dependent have depression.</p>
<p>Drinking alcohol frequently can make symptoms of depression worse, and people who have depression are more likely to misuse alcohol or
become dependent on it.</p>
