<h1>What’s the difference between a cold and the flu?</h1>
<p>Influenza or flu they both are respiratory illnesses that is cause by some virus and can cause similar symptoms.
But different viruses cause these two conditions.Your symptoms help you tell the difference between them.
Flu is highly contagious and is normally spread by the coughs and sneezes of an infected person.
A person can also infected by  flu by touching an infected person, for instance, by shaking hands.
Adults are contagious 1–2 days before getting symptoms and up to 7 days after becoming ill.
This means that you can spread the influenza virus before you even know you are infected.
Colds rarely cause other health conditions or problems. But the flu can lead to sinus and ear infections, pneumonia, and sepsis.
</p>
<p>To determine whether your symptoms are from a cold or from the flu, you need to see your doctor.
Your doctor will run tests that can help determine what’s behind your symptoms.Confusing flu with a bad cold is common.
  Both a cold and the flu share a few common symptoms.</p>
<h1>What are the symptoms of the flu?</h1>
<li>runny or stuffy nose</li>
<li>sneezing</li>
<li>body aches</li>
<li>general fatigue</li>
<li>Cough</li>
<li>Muscle aches</li>

<li>The <b>flu</b> always causes an increase in your body temperature.This is also known as a fever.
Most flu-related fevers range from a low-grade fever around 100°F (37°C) to as high as 104°F (40°C).</li>
<p>As a rule, flu symptoms are more severe than cold symptoms.</p>
<h1>Treatment</h1>
<p>A virus causes flu, so antibiotics cannot help, unless the flu has led to another illness caused by bacteria.</p>
<h1>Antivirals</h1>
<p>A doctor may prescribe antivirals, such as oseltamivir (Tamiflu) and zanamivir (Relenza), in some circumstances.</p>
<p>
In addition, in 2018, the United States Food and Drug Administration (FDA) approved a new drug—baloxavir marboxil
(Xofluza)—for acute uncomplicated influenza, or flu. People will take the drug by mouth, in a single dose.
</p>
<p>People can receive this treatment if they:</p>
<li>are aged 10 years or over</li>
<li>have had symptoms for fewer than 2 days</li>
<p>Possible side effects include diarrhea and bronchitis.</p>
<h1>Home remedies</h1>
<p>Individuals with flu should:</p>
<li>stay at home</li>
<li>avoid contact with other people where possible</li>
<li>keep warm and rest</li>
<li>consume plenty of liquids</li>
<li>avoid alcohol</li>
<li>stop smoking</li>
<li>eat if possible</li>
<p>It is a good idea for people that live alone to tell a relative, friend, or neighbor that they have flu and make sure
someone can check in on them.</p>
<h1>When to see a doctor</h1>
<p>A doctor only needs to know that a person has the flu if:</p>
<li>they are frail or older in age</li>
<li>their temperature remains high after 4–5 days</li>
<li>symptoms worsen</li>
<li>they feel seriously ill</li>
<li>they become short of breath, develop chest pain, or both</li>
<h1>Risks</h1>
<p>Flu is not usually serious, but it is unpleasant. For some people, however, there can be severe complications.</p>
<p>This is more likely in:</p>
<li>very young children</li>
<li>older people</li>
<li>individuals with other longstanding illness that can undermine their immune system</li>
<p>The risk of experiencing severe flu complications is higher for certain people:</p>
<li>adults over 65</li>
<li>babies or young children</li>
<li>pregnant women</li>
<li>individuals with heart or cardiovascular disease</li>
<li>those with chest problems, such as asthma or bronchitis</li>
<li>individuals with kidney disease</li>
<li>people with diabetes</li>
<li>people taking steroids</li>
<li>individuals undergoing treatment for cancer</li>
<li>those with longstanding diseases that reduce immune system function</li>
<p>The complications of influenza may include:</p>
<li>bacterial pneumonia</li>
<li>dehydration</li>
<li>worsening of chronic medical conditions, such as congestive heart failure, asthma, or diabetes</li>
<h1>Prevention</h1>
<p>Health experts and government agencies throughout the world say that the single best way to
protect oneself from catching flu is to have a flu vaccination every year.</p>
<p>There are two types of vaccinations:</p>
<li>the flu shot</li>
<li>the nasal-spray flu vaccine</li>
<h1>Seasonal flu shot</h1>
<p>A flu vaccine will contain three influenza viruses:</p>
<li>influenza (H3N2) virus</li>
<li>influenza (H1N2) virus</li>
<li>one B virus</li>
<h1>Flu Types</h1>
<li>influenza A</li>
<li>influenza B</li>
<li>influenza C</li>
<p>Types A and B viruses cause seasonal epidemics in the U.S. and Europe virtually every winter.</p>
<p>The type C influenza virus causes mild respiratory illness and is not responsible for outbreaks.</p>
