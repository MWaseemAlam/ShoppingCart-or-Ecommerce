<h1>What is Crohn’s disease?</h1>
<p>Crohn’s disease is a type of inflammatory bowel disease.Crohn's can affect any part of it, but most often it affects your small intestine
and colon.There’s no cure, but treatment can ease your symptoms and help you enjoy a full, active life.
It causes inflammation of your digestive tract, which can lead to abdominal pain, severe diarrhea, fatigue, weight loss and malnutrition.
Inflammation caused by Crohn's disease can involve different areas of the digestive tract in different people.
</p>
<h1>What Causes of Crohn’s disease?</h1>
<p>
In some people with Crohn's disease, only the last segment of the small intestine (ileum) is affected. In others, the disease is confined to the
colon (part of the large intestine). The most common areas affected by Crohn's disease are the last part of the small intestine and the colon.
It isn’t clear what causes Crohn’s disease. However, the following factors may influence whether you get it:
</p>
<li>your immune system</li>
<li>your genetics</li>
<li>your environment</li>
<li>Chronic diarrhea, often bloody and containing mucus or pus</li>
<li>Weight loss</li>

<h1>What Symptoms/sign of Crohn’s disease?</h1>
<li>Fever</li>
<li>Abdominal pain and tenderness</li>
<li>Feeling of a mass or fullness in the abdomen</li>
<li>Rectal bleeding</li>
<li>a loss of appetite</li>
<li>weight loss</li>
<li>blood in your stool</li>
<li>feeling a frequent need for bowel movements</li>
<h1>Other signs and symptoms</h1>
<p>People with severe Crohn's disease also may experience:</p>
<li>Inflammation of skin, eyes and joints</li>
<li>Inflammation of the liver or bile ducts</li>
<li>Delayed growth or sexual development, in children</li>
<p>It’s sometimes possible to mistake these symptoms for the symptoms of another condition, such as food poisoning, an upset stomach, or
an allergy. You should see your doctor if any of these symptoms persist.</p>
<p>The symptoms may become more severe as the disease progresses. These symptoms can include:</p>
<li>a perianal fistula, which causes pain and drainage near your anus</li>
<li>ulcers that may occur anywhere from the mouth to the anus</li>
<li>inflammation of the joints and skin</li>
<li>shortness of breath or decreased ability to exercise due to worsening anemia</li>
<h1>When to see a doctor:</h1>
<p>See your doctor if you have persistent changes in your bowel habits or if you have any of the signs and symptoms of Crohn's disease, such as:</p>
<li>Abdominal pain</li>
<li>Blood in your stool</li>
<li>Ongoing bouts of diarrhea that don't respond to over-the-counter (OTC) medications</li>
<li>Unexplained fever lasting more than a day or two</li>
<li>Unexplained weight loss</li>
<h1>Preventions:</h1>
<li>Adjust your fiber intake</li>
<li>Limit your fat intake</li>
<li>Limit your dairy intake</li>
<li>Drink water</li>
<li>Consider alternative sources of vitamins and minerals</li>
<h1>Treatment for Crohn’s disease:</h1>
<li>Medications</li>
<li>Dietary changes</li>
<li>Surgery</li>
<h1>Complications:</h1>
<p>Crohn's disease may lead to one or more of the following complications:</p>
<li><b>Bowel obstruction.</b> Crohn's disease affects the thickness of the intestinal wall. Over time, parts of the bowel can scar and narrow,
which may block the flow of digestive contents. You may require surgery to remove the diseased portion of your bowel.</li>
<li><b>Ulcers.</b>Chronic inflammation can lead to open sores (ulcers) anywhere in your digestive tract, including your mouth and anus,
  and in the genital area (perineum).</li>
<li><b>Fistulas.</b>
Sometimes ulcers can extend completely through the intestinal wall, creating a fistula — an abnormal connection between different body parts.
Fistulas can develop between your intestine and skin, or between your intestine and another organ. Fistulas near or around the anal area
(perianal) are the most common kind.
<br>
When fistulas develop in the abdomen, food may bypass areas of the bowel that are necessary for absorption. Fistulas may occur between loops
of bowel, into the bladder or vagina, or out through the skin, causing continuous drainage of bowel contents to your skin.
<br>
In some cases, a fistula may become infected and form an abscess, which can be life-threatening if not treated.
</li>
<li><b>Anal fissure.</b>
This is a small tear in the tissue that lines the anus or in the skin around the anus where infections can occur. It's often associated with
painful bowel movements and may lead to a perianal fistula.
</li>
<li>
<b>Malnutrition.</b>
Diarrhea, abdominal pain and cramping may make it difficult for you to eat or for your intestine to absorb enough nutrients to keep you
nourished. It's also common to develop anemia due to low iron or vitamin B-12 caused by the disease.
</li>
<li><b>Colon cancer.</b>
Having Crohn's disease that affects your colon increases your risk of colon cancer. General colon cancer screening guidelines for people
without Crohn's disease call for a colonoscopy every 10 years beginning at age 50. Ask your doctor whether you need to have this test done
sooner and more frequently.</li>
<li><b>Other health problems.</b>
Crohn's disease can cause problems in other parts of the body. Among these problems are anemia, skin disorders, osteoporosis, arthritis,
and gallbladder or liver disease.
</li>
<li><b>Medication risks.</b>
Certain Crohn's disease drugs that act by blocking functions of the immune system are associated with a small risk of developing cancers
such as lymphoma and skin cancers. They also increase risk of infection.
<br>
Corticosteroids can be associated with a risk of osteoporosis, bone fractures, cataracts, glaucoma, diabetes and high blood pressure,
among others. Work with your doctor to determine risks and benefits of medications.
</li>
