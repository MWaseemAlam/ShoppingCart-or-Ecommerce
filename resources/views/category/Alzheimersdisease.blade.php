<h1>What is Alzheimer’s disease?</h1>
<p>Alzheimer's disease is a neurological disorder in which the
death of brain cells waste away and die and also causes memory loss and cognitive decline.</p>
<p>Alzheimer's disease is the most common type of dementia in the United States and UK.</p>
<p>Dementia is characterized by a progressive and debilitating decline in cognition, function and behavior.
Dementia is a syndrome that is related with an ongoing decline of brain functioning.
It can affect thinking skills,memory and other mental abilities.</p>
<p>
The early signs of the disease may be forgetting recent events or conversations. As the disease progresses,
a person with Alzheimer's disease will develop severe memory impairment and lose the ability to carry out everyday tasks.
No treatment will cures Alzheimer's disease or alters the disease process in the brain.If the disease grow in its advanced stage,
complications from severe loss of brain function such as dehydration, malnutrition or infection and result in death.
</p>
