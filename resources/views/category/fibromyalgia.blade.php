<h1>What is fibromyalgia, and is it real?</h1>
<p>Fibromyalgia is a long-term or chronic disorder.People with fibromyalgia may also have pain in the sleep, memory,mood issues,muscles stiffness and bones,
areas of tenderness, and general fatigue. The above symptoms are subjective and they can not be determined or measured by tests.</p>
<p>The above Symptoms sometimes also begin after a physical trauma, surgery, infection or significant psychological stress. In other cases, symptoms gradually
accumulate over time with no single triggering event.</p>
<p>Fibromyalgia usually develop less in man then Women.Person who have fibromyalgia also have tension headaches, temporomandibular
joint (TMJ) disorders, irritable bowel syndrome, anxiety and depression.</p>
<p>There is no diagnose for fibromyalgia, a different kind  of medicense can help control symptoms. Exercise, relaxation and stress-reduction
measures also may help.</p>
