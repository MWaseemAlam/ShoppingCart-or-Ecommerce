<h1>What causes fibromyalgia?</h1>
<p>The actual cause of fibromyalgia is still not clear.However they are very near to understand the factors that may work together to
cause it.</p>
<ul>
<li>Breakdown of a relationship and death of loved one.</li>
<li>repetitive injury or infection</li>
</ul>
<p><b>Genetics:</b>Fibromyalgia also runs in families and there may be certain genetic mutations that develop fibromyalgia.
If your family member have these sign then the chances are very high developing fibromyalgia.</p>
<p><b>Trauma and Stress:</b>Fibromyalgia is also cause by a physical trauma, such as a road or car accident.Psychological stress may also trigger the condition.</p>
<h1>How fibromyalgia is treated</h1>
<p>Currently there is no exact cure for fibromyalgia, but some treatments help you get rid of some of the symptoms and make the
life easier.</p>
<p>Treatment tends to be a combination of Medical and self care</p>
<ul>
<li>medication, like painkillers and antidepressants</li>
<li>physical therapy and acupuncture</li>
<li>meditation</li>
<li>regular exercises and yoga</li>
<li>Sleep well at night</li>
<li>massage therapy</li>
<li>A healthy balanced diet </li>
</ul>
