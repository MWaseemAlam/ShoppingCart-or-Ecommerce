<h3>The most common types of treatment are:</h3>
<h1>Chemotherapy</h1>
<p>Use those medications that kill rapidly-dividing cancer/tumor cells.</p>

<h1>Surgery</h1>
<p>Surgery also removes cancer as much as possible.</p>

<h1>Radiation Therapy</h1>
<p>Uses powerful, focused beams of radiation inside (brachytherapy) or outside
(external beam radiation) your body to kill cancer cells.</p>
<h1>Stem Cell (Bone Marrow) Transplant</h1>
<p>Repairs diseased bone marrow with healthy stem cells. Stem cells are undifferentiated cells that can have a variety of functions.
These transplants allow doctors to use higher doses of chemotherapy to treat the cancer.</p>
<h1>Immunotherapy (Biological Therapy)</h1>
<p>Uses antibodies to help your body’s immune system recognize cancer so it can fight it off.</p>
<p>Hormone Therapy</p>
<p>Removes or blocks hormones that fuel certain cancers to stop cancer cells from growing.</p>
<h1>Targeted Drug Therapy</h1>
<p>Uses drugs to interfere with certain molecules that help cancer cells grow and survive.</p>
<h1>Clinical Trials:</h1>
<p>Investigates new ways to treat cancer.</p>
<h1>Alternative Medicine:</h1>
